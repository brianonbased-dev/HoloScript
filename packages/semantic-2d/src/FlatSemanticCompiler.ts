/**
 * @holoscript/core/compiler — Flat Semantic Compiler
 *
 * Compiles V6 @semantic_entity and @2d_canvas traits into a living React Three Fiber (R3F)
 * environment. Replaces the legacy "rectangles and CSS" Native2DCompiler by rendering
 * physics-aware UI overlaid on procedural WebGL fields.
 */

import { CompilerBase, type BaseCompilerOptions } from '@holoscript/core';
import type { HoloComposition, HoloObjectDecl } from '@holoscript/core-types/composition';

export interface FlatSemanticCompilerOptions extends BaseCompilerOptions {
  format?: 'react';
}

export class FlatSemanticCompiler extends CompilerBase {
  protected readonly compilerName = 'FlatSemanticCompiler';

  compile(
    composition: HoloComposition,
    agentToken: string,
    outputPath?: string,
    options?: FlatSemanticCompilerOptions
  ): string | any {
    this.validateCompilerAccess(agentToken, outputPath);

    const elements = composition.ui?.elements || composition.objects || [];

    // Extract canvas traits
    const traits = this.extractTraits(composition);
    const canvasConfig = traits['2d_canvas'] || {};

    const code = this.generateReactComponent(composition.name, elements, canvasConfig);

    if (options?.generateDocs) {
      return {
        output: code,
        documentation: this.generateDocumentation(composition, code, options.docsOptions),
      };
    }
    return code;
  }

  private extractTraits(obj: any): Record<string, any> {
    const map: Record<string, any> = {};
    if (!obj.traits) return map;
    for (const t of obj.traits) {
      map[t.name] = t.config || {};
    }
    return map;
  }

  // ============================================================================
  // CODE GENERATION
  // ============================================================================

  private generateReactComponent(
    name: string,
    objects: HoloObjectDecl[] | any[],
    _canvasConfig: any
  ): string {
    const safeName = name.replace(/[^a-zA-Z0-9]/g, '');

    const semanticNodes = objects
      .map((obj) => this.generateSemanticNode(obj))
      .join('\n            ');

    return `import React, { useRef, useState, useEffect } from 'react';
import { Canvas, useFrame } from '@react-three/fiber';
import { Html, Float, Sparkles, MeshDistortMaterial } from '@react-three/drei';

/**
 * HoloScript V6 2D UI Revolution
 * Flat-Semantic Projection of: ${name}
 * 
 * Auto-generated by FlatSemanticCompiler
 */

// Procedural Field Background Component
function ProceduralField({ turbulence = 0.3, intensity = 1.0 }) {
  const meshRef = useRef<any>(null);
  useFrame((state) => {
    if (meshRef.current) {
      meshRef.current.rotation.x = state.clock.getElapsedTime() * 0.1 * turbulence;
      meshRef.current.rotation.y = state.clock.getElapsedTime() * 0.15 * turbulence;
    }
  });

  return (
    <mesh ref={meshRef} scale={[15, 15, 1]} position={[0, 0, -5]}>
      <planeGeometry args={[2, 2, 32, 32]} />
      <MeshDistortMaterial 
        color="#1a0b2e" 
        emissive="#0a0515"
        distort={turbulence} 
        speed={intensity * 2} 
        roughness={0.2}
      />
      <Sparkles count={100} scale={20} size={2} color="#8b5cf6" speed={0.4} opacity={0.5} />
    </mesh>
  );
}

export function ${safeName}Component() {
  const [bountyPools, setBountyPools] = useState<Record<string, number>>({});

  return (
    <div className="holoscript-v6-root w-full h-screen bg-[#050505] overflow-hidden text-white font-sans">
      <Canvas orthographic camera={{ zoom: 100, position: [0, 0, 100] }}>
        <ambientLight intensity={0.5} />
        <directionalLight position={[10, 10, 5]} intensity={1} />
        
        {/* Simulation Layer */}
        <ProceduralField turbulence={0.3} intensity={1.0} />

        {/* Semantic 2D Projection Layer */}
        <Html fullscreen zIndexRange={[100, 0]} className="pointer-events-none">
          <div className="w-full h-full relative pointer-events-auto overflow-y-auto">
            ${semanticNodes}
          </div>
        </Html>
      </Canvas>
    </div>
  );
}

export default ${safeName}Component;
`;
  }

  private generateSemanticNode(obj: any): string {
    const traits = this.extractTraits(obj);

    // We analyze V6 traits to build the React tree
    const isEntity = !!traits['semantic_entity'];
    const type = traits['semantic_entity']?.type || 'generic';

    // Process layout and children
    let classes = 'relative ';
    if (traits.tailwind?.classes) classes += traits.tailwind.classes + ' ';

    // Semantic Layout Flow mapping
    const layout = traits['semantic_layout'];
    if (layout) {
      const flow = layout.flow || 'semantic';
      if (flow === 'cluster') classes += 'flex flex-wrap gap-4 justify-center items-center ';
      else if (flow === 'priority') classes += 'flex flex-col gap-3 ';
      else if (flow === 'radial')
        classes += 'grid place-items-center gap-4 p-8 rounded-full border border-white/10 ';
      else classes += 'flex flex-row gap-4 items-start ';

      const spacing = layout.spacing || 12;
      classes += `p-[${spacing}px] `;
    }

    // React to Agent Swarm Panels
    const hasSwarm = !!traits['agent_attention'];

    let rawChildren = obj.children || obj.objects || [];
    if (rawChildren.length === 0 && traits['semantic_entity']?.children) {
      rawChildren = traits['semantic_entity'].children;
    }

    const childrenMarkup = rawChildren
      .map((child: any) => this.generateSemanticNode(child))
      .join('\n');

    let content = traits.text?.content || '';
    if (content)
      content = '{`' + content.replace(new RegExp('`', 'g'), '\\`').replace(/\\$/g, '\\\\$') + '`}';

    // Semantic Mapping
    if (type === 'data-cluster' || type === 'section') {
      return (
        '<section className="' +
        classes +
        '">\n' +
        content +
        '\n' +
        childrenMarkup +
        '\n' +
        '</section>'
      );
    }

    if (type === 'metric-card') {
      return (
        '<div className="lift-card p-6 bg-white/5 border border-white/10 rounded-xl hover:bg-white/10 transition-all ' +
        classes +
        '">\n' +
        content +
        '\n' +
        childrenMarkup +
        '\n' +
        '</div>'
      );
    }

    // X402 Interactions
    let interactionProps = '';
    if (traits['agent_attention'] && traits['agent_attention'].bounty_threshold) {
      interactionProps =
        " onClick={() => alert('Bounty Negotiated: " +
        traits['agent_attention'].bounty_threshold +
        " credits')} ";
    } else if (traits['intent_driven']) {
      interactionProps =
        " onClick={() => console.log('Intent Emitted: " +
        (traits['intent_driven'].intents?.[0] || 'action') +
        "')} ";
    }

    if (traits.link || type === 'link') {
      return (
        '<a href="' +
        (traits.link?.href || '#') +
        '" className="' +
        classes +
        '"' +
        interactionProps +
        '>\n' +
        content +
        '\n' +
        childrenMarkup +
        '\n' +
        '</a>'
      );
    }

    if (traits.image) {
      return (
        '<img src="' +
        traits.image.src +
        '" className="' +
        classes +
        '" alt="' +
        (traits.image.alt || '') +
        '" />'
      );
    }

    // Default Fallback
    const swarmHtml = hasSwarm
      ? '<div className="text-xs text-purple-400 mt-2 font-mono flex items-center gap-2"><span className="w-2 h-2 rounded-full bg-purple-500 animate-pulse"></span> Agent Swarm Active</div>'
      : '';

    return (
      '<div className="' +
      classes +
      '"' +
      interactionProps +
      '>\n' +
      content +
      '\n' +
      swarmHtml +
      '\n' +
      childrenMarkup +
      '\n' +
      '</div>'
    );
  }
}
