#!/usr/bin/env tsx
/**
 * HoloScript Studio — .holo Page Compiler
 *
 * Compiles .holo files from holo-pages/ into Next.js App Router pages in src/app/.
 * Maintains .holo-manifest.json to track generated vs hand-written files.
 *
 * Usage:
 *   pnpm holo:build          # one-shot compile
 *   pnpm holo:build --watch  # watch mode for dev
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync, statSync } from 'fs';
import { join, relative, dirname, extname } from 'path';
import { parseHolo } from '@holoscript/core';
import { compileToNextJS } from '@holoscript/core/compiler/NextJSCompiler';

const STUDIO_ROOT = join(import.meta.dirname || __dirname, '..');
const HOLO_PAGES_DIR = join(STUDIO_ROOT, 'holo-pages');
const APP_DIR = join(STUDIO_ROOT, 'src', 'app');
const MANIFEST_PATH = join(STUDIO_ROOT, '.holo-manifest.json');

const GENERATED_HEADER = '// @generated by HoloScript NextJSCompiler — DO NOT EDIT\n';

// =============================================================================
// MANIFEST
// =============================================================================

interface Manifest {
  generatedFiles: string[];
  lastBuild: string;
}

function loadManifest(): Manifest {
  if (existsSync(MANIFEST_PATH)) {
    return JSON.parse(readFileSync(MANIFEST_PATH, 'utf-8'));
  }
  return { generatedFiles: [], lastBuild: '' };
}

function saveManifest(manifest: Manifest): void {
  writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2) + '\n');
}

// =============================================================================
// FILE DISCOVERY
// =============================================================================

function findHoloFiles(dir: string): string[] {
  const results: string[] = [];
  if (!existsSync(dir)) return results;

  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      results.push(...findHoloFiles(full));
    } else if (extname(entry) === '.holo') {
      results.push(full);
    }
  }

  return results;
}

// =============================================================================
// COMPILATION
// =============================================================================

function compileFile(holoPath: string): { outputPath: string; code: string } | null {
  const source = readFileSync(holoPath, 'utf-8');
  const relPath = relative(HOLO_PAGES_DIR, holoPath);

  // Parse
  const parseResult = parseHolo(source);
  if (!parseResult.success || !parseResult.ast) {
    console.error(`  ✗ Parse error in ${relPath}:`);
    for (const err of parseResult.errors) {
      console.error(`    Line ${err.line}: ${err.message}`);
    }
    return null;
  }

  // Compile to Next.js
  const result = compileToNextJS(parseResult.ast);

  // Determine output path: holo-pages/pipeline/page.holo → src/app/pipeline/page.tsx
  const dirPart = dirname(relPath);
  const outputPath = join(APP_DIR, dirPart, 'page.tsx');

  return { outputPath, code: GENERATED_HEADER + result.code };
}

function build(): void {
  const startTime = Date.now();
  const holoFiles = findHoloFiles(HOLO_PAGES_DIR);

  if (holoFiles.length === 0) {
    console.log('No .holo files found in holo-pages/');
    return;
  }

  console.log(`Compiling ${holoFiles.length} .holo page(s)...`);

  const manifest = loadManifest();
  const newGeneratedFiles: string[] = [];
  let successCount = 0;
  let errorCount = 0;

  for (const holoPath of holoFiles) {
    const relPath = relative(HOLO_PAGES_DIR, holoPath);
    const result = compileFile(holoPath);

    if (!result) {
      errorCount++;
      continue;
    }

    // Safety check: don't overwrite hand-written files
    const relOutput = relative(STUDIO_ROOT, result.outputPath);
    if (existsSync(result.outputPath)) {
      const existing = readFileSync(result.outputPath, 'utf-8');
      if (!existing.startsWith('// @generated') && !manifest.generatedFiles.includes(relOutput)) {
        console.error(`  ⚠ Skipping ${relOutput} — hand-written file (not in manifest)`);
        errorCount++;
        continue;
      }
    }

    // Write output
    mkdirSync(dirname(result.outputPath), { recursive: true });
    writeFileSync(result.outputPath, result.code);
    newGeneratedFiles.push(relOutput);
    successCount++;
    console.log(`  ✓ ${relPath} → ${relOutput}`);
  }

  // Update manifest
  manifest.generatedFiles = [...new Set([...manifest.generatedFiles, ...newGeneratedFiles])];
  manifest.lastBuild = new Date().toISOString();
  saveManifest(manifest);

  const elapsed = Date.now() - startTime;
  console.log(`\nDone: ${successCount} compiled, ${errorCount} errors (${elapsed}ms)`);
}

// =============================================================================
// WATCH MODE
// =============================================================================

async function watch(): Promise<void> {
  console.log('Watching holo-pages/ for changes...');

  // Initial build
  build();

  // Dynamic import chokidar (dev dependency)
  const { watch: chokidarWatch } = await import('chokidar');
  const watcher = chokidarWatch(join(HOLO_PAGES_DIR, '**/*.holo'), {
    ignoreInitial: true,
    awaitWriteFinish: { stabilityThreshold: 300 },
  });

  watcher.on('change', (path) => {
    console.log(`\n[holo:watch] ${relative(STUDIO_ROOT, path)} changed`);
    const result = compileFile(path);
    if (result) {
      mkdirSync(dirname(result.outputPath), { recursive: true });
      writeFileSync(result.outputPath, result.code);
      console.log(`  ✓ Recompiled`);
    }
  });

  watcher.on('add', (path) => {
    console.log(`\n[holo:watch] New file: ${relative(STUDIO_ROOT, path)}`);
    build(); // Rebuild all to update manifest
  });
}

// =============================================================================
// CLI
// =============================================================================

const isWatch = process.argv.includes('--watch');

if (isWatch) {
  watch().catch(console.error);
} else {
  build();
}
