/**
 * nodeGraphCompiler — walks React Flow edges and emits a GLSL fragment shader
 *
 * Algorithm:
 *  1. Build adjacency: for each node, which edges feed into which handles
 *  2. Topological sort (DFS post-order) starting from the output node
 *  3. Emit one GLSL line per node, forwarding results via tmp variables
 *  4. Return a complete fragment shader string
 *
 * Limitations (v1):
 *  - Single output node (fragColor)
 *  - Scalar float domain only (no vec3 chaining yet — output broadcasts float→vec3)
 *  - Math ops limited to the set defined in MathNodeData['op']
 */

import type { GNode, GEdge, MathNodeData } from './nodeGraphStore';

// ─── Types ────────────────────────────────────────────────────────────────────

interface CompiledResult {
  ok: boolean;
  glsl: string;
  error?: string;
}

type NodeMap = Map<string, GNode>;

// ─── Topological sort ─────────────────────────────────────────────────────────

function topoSort(nodes: GNode[], edges: GEdge[]): string[] {
  // Build: id → [ids that feed into id]
  const deps = new Map<string, string[]>();
  nodes.forEach((n) => deps.set(n.id, []));
  edges.forEach((e) => {
    const d = deps.get(e.target);
    if (d && !d.includes(e.source)) d.push(e.source);
  });

  const visited = new Set<string>();
  const order: string[] = [];

  function visit(id: string) {
    if (visited.has(id)) return;
    visited.add(id);
    (deps.get(id) ?? []).forEach(visit);
    order.push(id);
  }

  nodes.forEach((n) => visit(n.id));
  return order;
}

// ─── Runtime name for a node's primary output ─────────────────────────────────

function outVar(id: string) {
  return `v_${id.replace(/[^a-zA-Z0-9]/g, '_')}`;
}

// ─── Source for a handle ─────────────────────────────────────────────────────

function resolveSource(nodeId: string, handle: string, edges: GEdge[], fallback: string): string {
  const edge = edges.find((e) => e.target === nodeId && e.targetHandle === handle);
  return edge ? outVar(edge.source) : fallback;
}

// ─── Math op emission ─────────────────────────────────────────────────────────

const UNARY_OPS: MathNodeData['op'][] = ['sin', 'cos', 'length'];

function emitMath(op: MathNodeData['op'], a: string, b: string): string {
  if (UNARY_OPS.includes(op)) return `${op}(${a})`;
  if (op === 'mix') return `mix(${a}, ${b}, 0.5)`;
  if (op === 'dot') return `dot(vec2(${a}), vec2(${b}))`;
  const sym: Record<string, string> = { add: '+', sub: '-', mul: '*', div: '/', pow: ', ' };
  if (op === 'pow') return `pow(${a}, ${b})`;
  return `(${a} ${sym[op] ?? '+'} ${b})`;
}

// ─── Main compiler ────────────────────────────────────────────────────────────

export function compileNodeGraph(nodes: GNode[], edges: GEdge[]): CompiledResult {
  if (nodes.length === 0) {
    return { ok: false, glsl: '', error: 'Graph is empty' };
  }

  const outputNode = nodes.find((n) => n.data.type === 'output');
  if (!outputNode) {
    return { ok: false, glsl: '', error: 'No output node found' };
  }

  const nodeMap: NodeMap = new Map(nodes.map((n) => [n.id, n]));
  const order = topoSort(nodes, edges);

  const uniformLines: string[] = ['uniform float uTime;'];
  const bodyLines: string[] = [];

  // Collect texture uniforms
  nodes.forEach((n) => {
    if (n.data.type === 'texture') {
      uniformLines.push(`uniform sampler2D ${n.data.uniformName};`);
    }
  });

  // Emit declarations in topo order
  for (const id of order) {
    const node = nodeMap.get(id);
    if (!node) continue;
    const vout = outVar(id);
    const d = node.data;

    switch (d.type) {
      case 'constant':
        bodyLines.push(`float ${vout} = ${d.value.toFixed(4)};`);
        break;

      case 'time':
        bodyLines.push(`float ${vout} = uTime;`);
        break;

      case 'uv':
        bodyLines.push(`float ${vout} = vUv.x; // UV channel ${d.channel}`);
        break;

      case 'texture': {
        const uvSrc = resolveSource(id, 'uv', edges, 'vUv');
        bodyLines.push(
          `vec4 ${vout}_raw = texture2D(${d.uniformName}, ${uvSrc}_is_scalar ? vec2(${uvSrc}) : vUv);`
        );
        bodyLines.push(`float ${vout} = ${vout}_raw.r;`);
        break;
      }

      case 'math': {
        const a = resolveSource(id, 'a', edges, '0.0');
        const b = resolveSource(id, 'b', edges, '1.0');
        bodyLines.push(`float ${vout} = ${emitMath(d.op, a, b)};`);
        break;
      }

      case 'output': {
        const rgb = resolveSource(id, 'rgb', edges, '0.5');
        const alpha = resolveSource(id, 'alpha', edges, '1.0');
        bodyLines.push(`gl_FragColor = vec4(vec3(${rgb}), ${alpha});`);
        break;
      }
    }
  }

  const uniforms = uniformLines.join('\n');
  const body = bodyLines.map((l) => `  ${l}`).join('\n');

  const glsl = `// Generated by HoloScript Node Graph Compiler
// ${new Date().toISOString()}

varying vec2 vUv;
varying vec3 vNormal;
${uniforms}

void main() {
${body}
}`;

  return { ok: true, glsl };
}
