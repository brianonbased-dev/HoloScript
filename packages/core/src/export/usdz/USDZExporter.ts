/**
 * USDZ Exporter
 *
 * Exports HoloScript scene graphs to USDZ format for Apple Vision Pro and AR Quick Look.
 * Converts GLTF intermediate representation to USD, applies MaterialX materials,
 * and packages into USDZ archive.
 *
 * @module export/usdz
 * @version 1.0.0
 */

