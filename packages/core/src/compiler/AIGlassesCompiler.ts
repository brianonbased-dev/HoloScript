  private collectAllTraits(composition: HoloComposition): string[] {
    const traits: string[] = [];
    for (const obj of composition.objects ?? []) {
      this.collectObjectTraits(obj, traits);
    }
    return traits;
  }

  private collectObjectTraits(obj: HoloObjectDecl, traits: string[]): void {
    if (obj.traits) {
      for (const t of obj.traits) {
        const name = typeof t === 'string' ? t : t.name;
        if (!traits.includes(name)) traits.push(name);
      }
    }
    for (const child of obj.children ?? []) {
      this.collectObjectTraits(child, traits);
    }
  }

  private hasEventBlocks(composition: HoloComposition): boolean {
    // Check for event-like blocks in the composition
    if (composition.logic) return true;
    // Check objects for event traits
    for (const obj of composition.objects ?? []) {
      if (
        obj.traits?.some((t) => {
          const name = typeof t === 'string' ? t : t.name;
          return name === 'glasses_voice' || name === 'clickable';
        })
      )
        return true;
    }
    return false;
  }

  private extractVoiceCommands(composition: HoloComposition): string[] {
    const commands: string[] = [];

    // Extract from voice traits
    for (const obj of composition.objects ?? []) {
      if (obj.traits) {
        for (const t of obj.traits) {
          const name = typeof t === 'string' ? t : t.name;
          if (name === 'glasses_voice') {
            const config = typeof t === 'object' && t.config ? t.config : {};
            const cmds = (config as Record<string, unknown>).commands as string[] | undefined;
            if (cmds) commands.push(...cmds);
          }
        }
      }
    }

    // Extract from logic/event blocks (action names become voice commands)
    if (composition.logic) {
      const logicStr = JSON.stringify(composition.logic);
      const matches = logicStr.match(/"action"\s*:\s*"([^"]+)"/g);
      if (matches) {
        for (const m of matches) {
          const action = m.match(/"action"\s*:\s*"([^"]+)"/)?.[1];
          if (action && !commands.includes(action)) commands.push(action);
        }
      }
    }

    return commands;
  }

  /** Converts `#RRGGBB` or `#RRGGBBAA` to `Color(0xAARRGGBB)`. */
  private toKotlinColor(hex: string): string {
    if (typeof hex === 'string' && hex.startsWith('#')) {
      const raw = hex.slice(1);
      if (raw.length === 6) return `Color(0xFF${raw.toUpperCase()})`;
      if (raw.length === 8) {
        const [rr, gg, bb, aa] = [
          raw.substring(0, 2),
          raw.substring(2, 4),
          raw.substring(4, 6),
          raw.substring(6, 8),
        ];
        return `Color(0x${aa.toUpperCase()}${rr.toUpperCase()}${gg.toUpperCase()}${bb.toUpperCase()})`;
      }
    }
    return 'Color.White';
  }

  private toKotlinValue(value: HoloValue): string {
    if (typeof value === 'number') return Number.isInteger(value) ? `${value}` : `${value}f`;
    if (typeof value === 'boolean') return value ? 'true' : 'false';
    if (typeof value === 'string') return `"${this.escapeStringValue(value, 'Kotlin')}"`;
    if (value === null) return 'null';
    if (Array.isArray(value))
      return `listOf(${value.map((v) => this.toKotlinValue(v)).join(', ')})`;
    return 'null';
  }

  private findProp(obj: HoloObjectDecl, key: string): HoloValue | undefined {
    return obj.properties?.find((p) => p.key === key)?.value;
  }

  private sanitizeName(name: string): string {
    return name.replace(/[^a-zA-Z0-9_]/g, '_');
  }

  private emit(line: string): void {
    this.lines.push(this.options.indent.repeat(this.indentLevel) + line);
  }

  private indent(): void {
    this.indentLevel++;
  }

  private dedent(): void {
    this.indentLevel = Math.max(0, this.indentLevel - 1);
  }
