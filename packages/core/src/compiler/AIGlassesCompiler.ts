/**
 * HoloScript -> AI Glasses Compiler
 *
 * A distinct export target that emits Jetpack Compose Glimmer UI for
 * Android XR AI Glasses with transparent optical see-through displays.
 *
 * Target devices: Samsung Galaxy XR (2026), Warby Parker, Gentle Monster glasses.
 *
 * Unlike the AndroidXRCompiler's glasses mode (which is a secondary form factor
 * within the full Android XR pipeline), AIGlassesCompiler is a dedicated,
 * standalone compiler that:
 *
 *   1. Emits Kotlin Compose Glimmer layouts for transparent HUD overlays
 *   2. Maps HoloScript controller traits to touchpad gesture input
 *   3. Generates voice command handlers from HoloScript event blocks
 *   4. Respects AI Glasses form factor constraints:
 *      - No depth/3D rendering (additive 2D overlay)
 *      - Limited FOV (~30 degrees diagonal)
 *      - Ambient display (black = transparent)
 *      - Touchpad + voice input only (no hand/eye tracking)
 *      - 2-second animation transitions for heads-up comfort
 *   5. Uses AIGlassesTraitMap for glasses-specific trait coverage
 *
 * Emits:
 *   - activityFile: Glasses Activity with GlimmerTheme + Projected API
 *   - glimmerLayoutFile: Glimmer composable tree (Card, Text, Button, ListItem)
 *   - voiceHandlersFile: Voice command recognition handlers
 *   - manifestFile: Android manifest with xr_projected display category
 *   - buildGradle: Gradle with Glimmer + Projected dependencies
 *
 * @version 1.0.0 — Android XR SDK DP3 + Jetpack Compose Glimmer
 */

import { CompilerBase } from './CompilerBase';
