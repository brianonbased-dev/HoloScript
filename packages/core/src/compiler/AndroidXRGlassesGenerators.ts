export function emitGlassesActivityClass(compiler: AndroidXRCompiler, composition: HoloComposition): void {
  compiler.emit('@OptIn(ExperimentalProjectedApi::class)');
  compiler.emit(`class ${compiler.options.activityName} : ComponentActivity() {`);
  compiler.indent();

  // Glasses-specific state
  compiler.emit('private var displayController: ProjectedDisplayController? = null');
  compiler.emit('private var isVisualUiSupported by mutableStateOf(false)');
  compiler.emit('private var areVisualsOn by mutableStateOf(true)');
  if (composition.audio?.length) {
    compiler.emit('private lateinit var soundPool: SoundPool');
  }
  compiler.emit('');

  // onCreate
  compiler.emit('override fun onCreate(savedInstanceState: Bundle?) {');
  compiler.indent();
  compiler.emit('super.onCreate(savedInstanceState)');
  compiler.emit('');
  compiler.emit('// Detect glasses display capability via Projected API');
  compiler.emit('lifecycleScope.launch {');
  compiler.indent();
  compiler.emit(
    'val projectedDeviceController = ProjectedDeviceController.create(this@' +
      compiler.options.activityName +
      ')'
  );
  compiler.emit(
    'isVisualUiSupported = projectedDeviceController.capabilities.contains(CAPABILITY_VISUAL_UI)'
  );
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  if (composition.audio?.length) {
    compiler.emit('val audioAttrs = AudioAttributes.Builder()');
    compiler.indent();
    compiler.emit('.setUsage(AudioAttributes.USAGE_GAME)');
    compiler.emit('.setContentType(AudioAttributes.CONTENT_TYPE_SONIFICATION).build()');
    compiler.dedent();
    compiler.emit(
      'soundPool = SoundPool.Builder().setMaxStreams(8).setAudioAttributes(audioAttrs).build()'
    );
    compiler.emit('');
  }

  compiler.emit('setContent {');
  compiler.indent();
  compiler.emit('GlimmerTheme {');
  compiler.indent();
  compiler.emit(`${compiler.sanitizeName(composition.name)}GlassesScreen(`);
  compiler.indent();
  compiler.emit('areVisualsOn = areVisualsOn,');
  compiler.emit('isVisualUiSupported = isVisualUiSupported,');
  compiler.emit(`onClose = { finish() }`);
  compiler.dedent();
  compiler.emit(')');
  compiler.dedent();
  compiler.emit('}');
  compiler.dedent();
  compiler.emit('}');
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  // Main Glimmer composable
  emitGlassesSceneComposable(compiler, composition);

  compiler.dedent();
  compiler.emit('}');
}

export function emitGlassesSceneComposable(compiler: AndroidXRCompiler, composition: HoloComposition): void {
  const name = compiler.sanitizeName(composition.name);

  compiler.emit('@Composable');
  compiler.emit(`private fun ${name}GlassesScreen(`);
  compiler.indent();
  compiler.emit('areVisualsOn: Boolean,');
  compiler.emit('isVisualUiSupported: Boolean,');
  compiler.emit('onClose: () -> Unit,');
  compiler.emit('modifier: Modifier = Modifier');
  compiler.dedent();
  compiler.emit(') {');
  compiler.indent();

  // State from composition
  if (composition.state) {
    for (const p of composition.state.properties) {
      compiler.emit(
        `var ${compiler.escapeStringValue(p.key as string, 'Kotlin')} by remember { mutableStateOf(${compiler.toKotlinValue(p.value)}) }`
      );
    }
    compiler.emit('');
  }

  // Outer Box with Glimmer surface modifier
  compiler.emit('Box(');
  compiler.indent();
  compiler.emit('modifier = modifier');
  compiler.indent();
  compiler.emit('.surface(focusable = false)');
  compiler.emit('.fillMaxSize(),');
  compiler.dedent();
  compiler.emit('contentAlignment = Alignment.Center');
  compiler.dedent();
  compiler.emit(') {');
  compiler.indent();

  // Branch on visual UI capability
  compiler.emit('if (isVisualUiSupported && areVisualsOn) {');
  compiler.indent();

  // Emit Glimmer UI tree
  emitGlimmerContentTree(compiler, composition);

  compiler.dedent();
  compiler.emit('} else {');
  compiler.indent();
  compiler.emit('// Audio-only fallback for glasses without display');
  compiler.emit('Text("Audio Guidance Mode Active")');
  compiler.dedent();
  compiler.emit('}');

  compiler.dedent();
  compiler.emit('}');

  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');
}

export function emitGlimmerContentTree(compiler: AndroidXRCompiler, composition: HoloComposition): void {
  compiler.emit('Column(');
  compiler.indent();
  compiler.emit('modifier = Modifier.fillMaxSize(),');
  compiler.emit('verticalArrangement = Arrangement.spacedBy(8.dp)');
  compiler.dedent();
  compiler.emit(') {');
  compiler.indent();

  // Title chip for composition name
  compiler.emit(
    `TitleChip(title = "${compiler.escapeStringValue(composition.name as string, 'Kotlin')}")`
  );
  compiler.emit('');

  // Convert objects to Glimmer cards
  for (const obj of composition.objects ?? []) {
    emitGlimmerObject(compiler, obj);
  }

  // UI elements as Glimmer components
  if (composition.ui) {
    compiler.emit('');
    compiler.emit('// UI Elements');
    for (const el of composition.ui.elements) {
      emitGlimmerUIElement(compiler, el);
    }
  }

  // Close button
  compiler.emit('');
  compiler.emit('Button(onClick = onClose) {');
  compiler.indent();
  compiler.emit('Text("Close")');
  compiler.dedent();
  compiler.emit('}');

  compiler.dedent();
  compiler.emit('}');
}

export function emitGlimmerObject(compiler: AndroidXRCompiler, obj: HoloObjectDecl): void {
  const v = compiler.sanitizeName(obj.name);
  const meshType = compiler.findProp(obj, 'mesh') ?? compiler.findProp(obj, 'type') ?? 'object';
  const modelSrc = compiler.findProp(obj, 'model') ?? compiler.findProp(obj, 'src');
  const text = compiler.findProp(obj, 'text');

  compiler.emit(
    `// Object: ${compiler.escapeStringValue(obj.name as string, 'Kotlin')} (glasses overlay)`
  );

  if (text) {
    // Text objects become plain Text composables
    const color = compiler.findProp(obj, 'color');
    compiler.emit(
      `Text("${text}"${color ? `, color = ${compiler.toKotlinColor(color as string)}` : ''})`
    );
  } else if (modelSrc) {
    // 3D model objects become Card with model info
    compiler.emit(`Card(`);
    compiler.indent();
    compiler.emit(`title = { Text("${compiler.escapeStringValue(obj.name as string, 'Kotlin')}") },`);
    compiler.emit(`action = {`);
    compiler.indent();
    compiler.emit(`Button(onClick = { /* interact with ${v} */ }) {`);
    compiler.indent();
    compiler.emit('Text("View")');
    compiler.dedent();
    compiler.emit('}');
    compiler.dedent();
    compiler.emit('}');
    compiler.dedent();
    compiler.emit(') {');
    compiler.indent();
    compiler.emit(`Text("3D Model: ${modelSrc}")`);
    const pos = compiler.findProp(obj, 'position');
    if (pos && Array.isArray(pos)) {
      compiler.emit(`Text("Position: ${(pos as number[]).join(', ')}")`);
    }
    compiler.dedent();
    compiler.emit('}');
  } else {
    // Primitive objects become ListItems
    compiler.emit(`ListItem(`);
    compiler.indent();
    compiler.emit(
      `headlineContent = { Text("${compiler.escapeStringValue(obj.name as string, 'Kotlin')}") },`
    );
    compiler.emit(`supportingContent = { Text("Type: ${meshType}") }`);
    compiler.dedent();
    compiler.emit(')');
  }
  compiler.emit('');

  // Recurse children
  for (const child of obj.children ?? []) {
    emitGlimmerObject(compiler, child);
  }
}

export function emitGlimmerUIElement(compiler: AndroidXRCompiler, el: {
  name: string;
  properties: Array<{ key: string; value: unknown }>;
}): void {
  const vn = compiler.sanitizeName(el.name);
  const text = el.properties.find((p) => p.key === 'text')?.value;
  const label = el.properties.find((p) => p.key === 'label')?.value;
  const elType = el.properties.find((p) => p.key === 'type')?.value;

  if (elType === 'button' && label) {
    compiler.emit(`Button(onClick = { /* ${vn} */ }) { Text("${label}") }`);
  } else if (text) {
    const color = el.properties.find((p) => p.key === 'color')?.value;
    compiler.emit(
      `Text("${text}"${color ? `, color = ${compiler.toKotlinColor(color as string)}` : ''})`
    );
  } else {
    compiler.emit(
      `Card(title = { Text("${compiler.escapeStringValue(el.name as string, 'Kotlin')}") }) {`
    );
    compiler.indent();
    compiler.emit(`Text("${compiler.escapeStringValue(el.name as string, 'Kotlin')}")`);
    compiler.dedent();
    compiler.emit('}');
  }
}

export function generateGlassesManifestFile(compiler: AndroidXRCompiler, composition: HoloComposition): string {
  const pkg = compiler.options.packageName;
  const activityName = compiler.options.activityName;

  const permissions: string[] = [];
  permissions.push('    <uses-permission android:name="android.permission.CAMERA" />');
  permissions.push('    <uses-permission android:name="android.permission.RECORD_AUDIO" />');
  permissions.push('    <uses-permission android:name="android.permission.BLUETOOTH_CONNECT" />');

  const hasHandTracking = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'hand_tracking')
  );
  if (hasHandTracking) {
    permissions.push('    <uses-permission android:name="android.permission.HAND_TRACKING" />');
  }

  return `<?xml version="1.0" encoding="utf-8"?>
<!-- Auto-generated by HoloScript AndroidXRCompiler (AI Glasses mode) -->
<manifest xmlns:android="http://schemas.android.com/apk/res/android"
    package="${pkg}">

    <!-- Glasses Permissions -->
${permissions.join('\n')}

    <application
        android:allowBackup="true"
        android:label="${compiler.escapeStringValue(composition.name as string, 'XML')}"
        android:supportsRtl="true"
        android:theme="@style/Theme.MaterialComponents.DayNight.NoActionBar">

        <!-- Android XR Glasses metadata -->
        <meta-data android:name="com.google.ar.core" android:value="optional" />

        <activity
            android:name=".${activityName}"
            android:exported="true"
            android:requiredDisplayCategory="xr_projected"
            android:label="${compiler.escapeStringValue(composition.name as string, 'XML')} AI Glasses">
            <intent-filter>
                <action android:name="android.intent.action.MAIN" />
            </intent-filter>
        </activity>
    </application>
</manifest>`;
}

