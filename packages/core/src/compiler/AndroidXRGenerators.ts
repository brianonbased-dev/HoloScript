// ─── DP3: Head-Following UI via UserSubspace ──────────────────────

export function emitHeadFollowUI(
  compiler: AndroidXRCompiler,
  varName: string,
  obj: HoloObjectDecl,
  config: Record<string, unknown>
): void {
  const distance = config.distance ?? config.follow_distance ?? 1.5;
  const w = compiler.findProp(obj, 'width') ?? config.width ?? 400;
  const h = compiler.findProp(obj, 'height') ?? config.height ?? 300;
  compiler.emit('');
  compiler.emit(`// DP3: Head-following UI panel via UserSubspace`);
  compiler.emit(`// android.permission.HEAD_TRACKING required`);
  compiler.emit(`// Content follows user's head at ${distance}m distance (soft-locking)`);
  compiler.emit(`SpatialPanel(SubspaceModifier.width(${w}f).height(${h}f) {`);
  compiler.indent();
  const text = compiler.findProp(obj, 'text');
  const label = compiler.findProp(obj, 'label');
  if (text) {
    compiler.emit(`Text("${text}")`);
  } else if (label) {
    compiler.emit(`Text("${label}")`);
  } else {
    compiler.emit(
      `Column(modifier = Modifier.fillMaxSize()) { Text("${compiler.escapeStringValue(obj.name as string, 'Kotlin')}") }`
    );
  }
  compiler.dedent();
  compiler.emit('}');
}

// ─── DP3: DRM-Protected Video via SurfaceEntity ───────────────────

export function emitDrmVideo(compiler: AndroidXRCompiler, varName: string, config: Record<string, unknown>): void {
  const videoUri = config.uri ?? config.src ?? config.source ?? '';
  const licenseUri = config.license_uri ?? config.drm_license ?? '';
  const stereoMode = (config.stereo_mode as string) ?? 'SIDE_BY_SIDE';
  const shape = (config.shape as string) ?? 'quad';
  const width = config.width ?? 1.0;
  const height = config.height ?? 1.0;
  const radius = config.radius ?? 5.0;
  compiler.emit('');
  compiler.emit(`// DP3: DRM-protected video via SurfaceEntity + Widevine`);

  // Determine shape
  let shapeCode: string;
  if (shape === 'sphere') {
    shapeCode = `SurfaceEntity.Shape.Sphere(${radius}f)`;
  } else if (shape === 'hemisphere') {
    shapeCode = `SurfaceEntity.Shape.Hemisphere(${radius}f)`;
  } else {
    shapeCode = `SurfaceEntity.Shape.Quad(FloatSize2d(${width}f, ${height}f))`;
  }

  const stereoModeMap: Record<string, string> = {
    SIDE_BY_SIDE: 'SurfaceEntity.StereoMode.SIDE_BY_SIDE',
    TOP_BOTTOM: 'SurfaceEntity.StereoMode.TOP_BOTTOM',
    MONO: 'SurfaceEntity.StereoMode.MONO',
    MULTIVIEW_LEFT: 'SurfaceEntity.StereoMode.MULTIVIEW_LEFT_PRIMARY',
    MULTIVIEW_RIGHT: 'SurfaceEntity.StereoMode.MULTIVIEW_RIGHT_PRIMARY',
  };
  const stereoModeKotlin = stereoModeMap[stereoMode] ?? 'SurfaceEntity.StereoMode.SIDE_BY_SIDE';

  compiler.emit(`val ${varName}Surface = SurfaceEntity.create(`);
  compiler.indent();
  compiler.emit('session = xrSession,');
  compiler.emit(`stereoMode = ${stereoModeKotlin},`);
  compiler.emit('pose = Pose(Vector3(0f, 0f, -1.5f), Quaternion.identity()),');
  compiler.emit(`shape = ${shapeCode},`);
  compiler.emit('surfaceProtection = SurfaceEntity.SurfaceProtection.PROTECTED');
  compiler.dedent();
  compiler.emit(')');

  if (videoUri) {
    compiler.emit('');
    compiler.emit(`val ${varName}MediaItem = MediaItem.Builder()`);
    compiler.indent();
    compiler.emit(`.setUri("${videoUri}")`);
    if (licenseUri) {
      compiler.emit('.setDrmConfiguration(');
      compiler.indent();
      compiler.emit('MediaItem.DrmConfiguration.Builder(C.WIDEVINE_UUID)');
      compiler.indent();
      compiler.emit(`.setLicenseUri("${licenseUri}")`);
      compiler.emit('.build()');
      compiler.dedent();
      compiler.dedent();
      compiler.emit(')');
    }
    compiler.emit('.build()');
    compiler.dedent();
    compiler.emit('');
    compiler.emit(`val ${varName}Player = ExoPlayer.Builder(this).build()`);
    compiler.emit(`${varName}Player.setVideoSurface(${varName}Surface.getSurface())`);
    compiler.emit(`${varName}Player.setMediaItem(${varName}MediaItem)`);
    compiler.emit(`${varName}Player.prepare()`);
    compiler.emit(`${varName}Player.play()`);
  }
}

// ─── Domain Blocks (v4.2) ──────────────────────────────────────────

export function compileAndroidXRDomainBlocks(compiler: AndroidXRCompiler, composition: HoloComposition): void {
  const domainBlocks =
    ((composition as unknown as Extensible<HoloComposition>).domainBlocks as unknown[]) ?? [];
  if (domainBlocks.length === 0) return;

  compiler.emit('');
  compiler.emit('// === v4.2 Domain Blocks ===');

  let blockIdx = 0;
  const compiled = compileDomainBlocks(
    // @ts-expect-error During migration
    domainBlocks,
    {
      material: (block) => {
        const mat = compileMaterialBlock(block);
        return materialToAndroidXR(mat, `db${blockIdx++}`);
      },
      physics: (block) => {
        const phys = compilePhysicsBlock(block);
        return physicsToAndroidXR(phys, `db${blockIdx++}`);
      },
      vfx: (block) => {
        const ps = compileParticleBlock(block);
        return particlesToAndroidXR(ps, `db${blockIdx++}`);
      },
      postfx: (block) => {
        const pp = compilePostProcessingBlock(block);
        const postfx = pp as CompiledPostProcessing;
        const effects = postfx.effects
          .map((e) => `// Effect: ${e.type} — ${JSON.stringify(e.properties)}`)
          .join('\n');
        return `// Post-Processing: ${postfx.keyword} — configure via Filament Renderer post-processing\n${effects}`;
      },
      audio: (block) => {
        const audio = compileAudioSourceBlock(block);
        return audioSourceToAndroidXR(audio, `db${blockIdx++}`);
      },
      weather: (block) => {
        const weather = compileWeatherBlock(block);
        return weatherToAndroidXR(weather);
      },
    },
    (block) =>
      `// Domain block: ${block.domain}/${block.keyword} "${compiler.escapeStringValue(block.name as string, 'Kotlin')}"`
  );

  for (const line of compiled) {
    for (const l of line.split('\n')) {
      compiler.emit(l);
    }
  }
}

// ─── Transitions ─────────────────────────────────────────────────────

export function emitTransition(compiler: AndroidXRCompiler, tr: HoloTransition): void {
  const fn = compiler.sanitizeName(tr.name);
  const target = tr.properties.find((p) => p.key === 'target')?.value;
  const dur = tr.properties.find((p) => p.key === 'duration')?.value ?? 0.5;
  compiler.emit('');
  compiler.emit(`private fun transition_${fn}() {`);
  compiler.indent();
  compiler.emit(`// -> "${target}"`);
  compiler.emit(
    `ObjectAnimator.ofFloat(null, "alpha", 1f, 0f).apply { duration = ${Math.round((dur as number) * 1000)}L }.start()`
  );
  compiler.dedent();
  compiler.emit('}');
}
