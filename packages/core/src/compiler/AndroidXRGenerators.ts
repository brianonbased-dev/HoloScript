export function generateActivityFile(compiler: AndroidXRCompiler, composition: HoloComposition): string {
  compiler.lines = [];
  compiler.indentLevel = 0;

  compiler.emit('// Auto-generated by HoloScript AndroidXRCompiler');
  compiler.emit(
    `// Source: composition "${compiler.escapeStringValue(composition.name as string, 'Kotlin')}"`
  );
  compiler.emit('// Do not edit manually -- regenerate from .holo source');
  if (compiler.options.provenanceHash) {
    compiler.emit(`// Provenance Hash: ${compiler.options.provenanceHash}`);
  }
  compiler.emit('');
  compiler.emit(`package ${compiler.options.packageName}`);
  compiler.emit('');
  emitImports(compiler, composition);
  compiler.emit('');
  emitActivityClass(compiler, composition);

  // v4.2: Domain Blocks (materials, physics, particles, post-processing, audio, weather)
  compileAndroidXRDomainBlocks(compiler, composition);

  return compiler.lines.join('\n');
}

// ─── State File ─────────────────────────────────────────────────────

export function generateStateFile(compiler: AndroidXRCompiler, composition: HoloComposition): string {
  compiler.lines = [];
  compiler.indentLevel = 0;

  const pkg = compiler.options.packageName;

  compiler.emit('// Auto-generated by HoloScript AndroidXRCompiler');
  compiler.emit(`// State: ${compiler.escapeStringValue(composition.name as string, 'Kotlin')}`);
  compiler.emit('');
  compiler.emit(`package ${pkg}`);
  compiler.emit('');
  compiler.emit('import androidx.compose.runtime.mutableStateOf');
  compiler.emit('import androidx.compose.runtime.getValue');
  compiler.emit('import androidx.compose.runtime.setValue');
  compiler.emit('import androidx.lifecycle.ViewModel');
  compiler.emit('import androidx.lifecycle.MutableLiveData');
  compiler.emit('import androidx.lifecycle.LiveData');
  compiler.emit('import androidx.xr.runtime.math.Vector3');
  compiler.emit('');

  compiler.emit('class XRSceneState : ViewModel() {');
  compiler.indent();

  // State properties from composition
  if (composition.state) {
    compiler.emit('// === State Properties ===');
    for (const prop of composition.state.properties) {
      const kotlinType = compiler.toKotlinType(prop.value);
      const kotlinValue = compiler.toKotlinValue(prop.value);
      compiler.emit(
        `private val _${compiler.escapeStringValue(prop.key as string, 'Kotlin')} = MutableLiveData(${kotlinValue})`
      );
      compiler.emit(
        `val ${compiler.escapeStringValue(prop.key as string, 'Kotlin')}: LiveData<${kotlinType}> get() = _${compiler.escapeStringValue(prop.key as string, 'Kotlin')}`
      );
      compiler.emit('');
    }
  }

  // XR-specific state
  compiler.emit('// === XR State ===');
  compiler.emit('private val _spatialCapabilities = MutableLiveData<Set<String>>(emptySet())');
  compiler.emit('val spatialCapabilities: LiveData<Set<String>> get() = _spatialCapabilities');
  compiler.emit('');

  compiler.emit(
    'private val _activeEntities = MutableLiveData<MutableMap<String, Any>>(mutableMapOf())'
  );
  compiler.emit('val activeEntities: LiveData<MutableMap<String, Any>> get() = _activeEntities');
  compiler.emit('');

  compiler.emit('private val _selectedEntity = MutableLiveData<String?>()');
  compiler.emit('val selectedEntity: LiveData<String?> get() = _selectedEntity');
  compiler.emit('');

  // Entity interaction callback
  compiler.emit('fun onEntitySelected(entityId: String) {');
  compiler.indent();
  compiler.emit('_selectedEntity.value = entityId');
  compiler.emit('android.util.Log.d("HoloScript", "Entity selected: $entityId")');
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  // Spatial capability update
  compiler.emit('fun updateSpatialCapabilities(capabilities: Set<String>) {');
  compiler.indent();
  compiler.emit('_spatialCapabilities.value = capabilities');
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  // Reset
  compiler.emit('fun reset() {');
  compiler.indent();
  compiler.emit('_spatialCapabilities.value = emptySet()');
  compiler.emit('_activeEntities.value = mutableMapOf()');
  compiler.emit('_selectedEntity.value = null');
  if (composition.state) {
    for (const prop of composition.state.properties) {
      const kotlinValue = compiler.toKotlinValue(prop.value);
      compiler.emit(
        `_${compiler.escapeStringValue(prop.key as string, 'Kotlin')}.value = ${kotlinValue}`
      );
    }
  }
  compiler.emit('android.util.Log.d("HoloScript", "XR State reset")');
  compiler.dedent();
  compiler.emit('}');

  // Actions
  if (composition.logic?.actions) {
    compiler.emit('');
    compiler.emit('// === Actions ===');
    for (const action of composition.logic.actions) {
      compileAction(compiler, action);
    }
  }

  compiler.dedent();
  compiler.emit('}');

  return compiler.lines.join('\n');
}

export function compileAction(compiler: AndroidXRCompiler, action: { name: string }): void {
  const rawName = compiler.sanitizeName(action.name);
  const name = rawName.charAt(0).toLowerCase() + rawName.slice(1);
  compiler.emit(`fun ${name}() {`);
  compiler.indent();
  compiler.emit(
    `android.util.Log.d("HoloScript", "Action: ${compiler.escapeStringValue(action.name, 'Kotlin')}")`
  );
  compiler.emit('// Action implementation');
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');
}

// ─── Node Factory File ──────────────────────────────────────────────

export function generateNodeFactoryFile(compiler: AndroidXRCompiler, composition: HoloComposition): string {
  compiler.lines = [];
  compiler.indentLevel = 0;

  const pkg = compiler.options.packageName;

  compiler.emit('// Auto-generated by HoloScript AndroidXRCompiler');
  compiler.emit(`// Node Factory: ${compiler.escapeStringValue(composition.name as string, 'Kotlin')}`);
  compiler.emit('');
  compiler.emit(`package ${pkg}`);
  compiler.emit('');
  compiler.emit('import android.net.Uri');
  compiler.emit('import androidx.xr.scenecore.Session as XRSession');
  compiler.emit('import androidx.xr.scenecore.Entity');
  compiler.emit('import androidx.xr.scenecore.GltfModel');
  compiler.emit('import androidx.xr.scenecore.GltfModelEntity');
  compiler.emit('import androidx.xr.runtime.math.Pose');
  compiler.emit('import androidx.xr.runtime.math.Vector3');
  compiler.emit('import androidx.xr.runtime.math.Quaternion');
  compiler.emit('');

  compiler.emit('object XRNodeFactory {');
  compiler.indent();

  // Default entity creation
  compiler.emit('/**');
  compiler.emit(' * Create a default entity in the XR scene.');
  compiler.emit(' */');
  compiler.emit('fun createDefaultEntity(session: XRSession): Entity {');
  compiler.indent();
  if (composition.objects?.length) {
    const firstObj = composition.objects[0];
    const modelSrc = compiler.findProp(firstObj, 'model') ?? compiler.findProp(firstObj, 'src');
    if (modelSrc) {
      compiler.emit(`val gltfModel = GltfModel.create(session, Uri.parse("${modelSrc}"))`);
      compiler.emit('return GltfModelEntity.create(session, gltfModel).apply {');
      compiler.indent();
      compiler.emit('parent = session.scene.activitySpace');
      const pos = compiler.findProp(firstObj, 'position');
      if (pos && Array.isArray(pos)) {
        compiler.emit(
          `setPose(Pose(${compiler.toKotlinFloat3(pos as number[])}, Quaternion.identity()))`
        );
      }
      compiler.dedent();
      compiler.emit('}');
    } else {
      compiler.emit(
        `val entity = session.scene.createEntity("${compiler.escapeStringValue(firstObj.name as string, 'Kotlin')}")`
      );
      compiler.emit('entity.parent = session.scene.activitySpace');
      const pos = compiler.findProp(firstObj, 'position');
      if (pos && Array.isArray(pos)) {
        compiler.emit(
          `entity.setPose(Pose(${compiler.toKotlinFloat3(pos as number[])}, Quaternion.identity()))`
        );
      }
      compiler.emit('return entity');
    }
  } else {
    compiler.emit('val entity = session.scene.createEntity("default")');
    compiler.emit('entity.parent = session.scene.activitySpace');
    compiler.emit('return entity');
  }
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  // GltfModel loader
  compiler.emit('/**');
  compiler.emit(' * Load a glTF model and create a GltfModelEntity.');
  compiler.emit(' */');
  compiler.emit('fun loadGltfModel(');
  compiler.indent();
  compiler.emit('session: XRSession,');
  compiler.emit('modelUri: String,');
  compiler.emit('position: Vector3 = Vector3(0f, 0f, 0f),');
  compiler.emit('scale: Float = 1f');
  compiler.dedent();
  compiler.emit('): GltfModelEntity {');
  compiler.indent();
  compiler.emit('val gltfModel = GltfModel.create(session, Uri.parse(modelUri))');
  compiler.emit('return GltfModelEntity.create(session, gltfModel).apply {');
  compiler.indent();
  compiler.emit('parent = session.scene.activitySpace');
  compiler.emit('setPose(Pose(position, Quaternion.identity()))');
  compiler.emit('setScale(com.google.android.filament.utils.Float3(scale, scale, scale))');
  compiler.dedent();
  compiler.emit('}');
  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');

  // Named entity factory methods for each object
  for (const obj of composition.objects || []) {
    compileObjectFactory(compiler, obj);
  }

  compiler.dedent();
  compiler.emit('}');

  return compiler.lines.join('\n');
}

export function compileObjectFactory(compiler: AndroidXRCompiler, obj: HoloObjectDecl): void {
  const methodName = `create${compiler.sanitizeName(obj.name).charAt(0).toUpperCase() + compiler.sanitizeName(obj.name).slice(1)}`;
  const modelSrc = compiler.findProp(obj, 'model') ?? compiler.findProp(obj, 'src');

  compiler.emit(`fun ${methodName}(session: XRSession): Entity {`);
  compiler.indent();

  if (modelSrc) {
    compiler.emit(`val gltfModel = GltfModel.create(session, Uri.parse("${modelSrc}"))`);
    compiler.emit('return GltfModelEntity.create(session, gltfModel).apply {');
    compiler.indent();
    compiler.emit('parent = session.scene.activitySpace');
    const pos = compiler.findProp(obj, 'position');
    if (pos && Array.isArray(pos)) {
      compiler.emit(`setPose(Pose(${compiler.toKotlinFloat3(pos as number[])}, Quaternion.identity()))`);
    }
    const scale = compiler.findProp(obj, 'scale');
    if (scale && Array.isArray(scale)) {
      compiler.emit(`setScale(${compiler.toKotlinFloat3(scale as number[])})`);
    } else if (typeof scale === 'number') {
      compiler.emit(
        `setScale(com.google.android.filament.utils.Float3(${scale}f, ${scale}f, ${scale}f))`
      );
    }
    compiler.dedent();
    compiler.emit('}');
  } else {
    compiler.emit(
      `val entity = session.scene.createEntity("${compiler.escapeStringValue(obj.name as string, 'Kotlin')}")`
    );
    compiler.emit('entity.parent = session.scene.activitySpace');
    const pos = compiler.findProp(obj, 'position');
    if (pos && Array.isArray(pos)) {
      compiler.emit(
        `entity.setPose(Pose(${compiler.toKotlinFloat3(pos as number[])}, Quaternion.identity()))`
      );
    }
    const scale = compiler.findProp(obj, 'scale');
    if (scale && Array.isArray(scale)) {
      compiler.emit(`entity.setScale(${compiler.toKotlinFloat3(scale as number[])})`);
    } else if (typeof scale === 'number') {
      compiler.emit(
        `entity.setScale(com.google.android.filament.utils.Float3(${scale}f, ${scale}f, ${scale}f))`
      );
    }
    compiler.emit('return entity');
  }

  compiler.dedent();
  compiler.emit('}');
  compiler.emit('');
}

// ─── Manifest File ──────────────────────────────────────────────────

export function generateManifestFile(compiler: AndroidXRCompiler, composition: HoloComposition): string {
  const pkg = compiler.options.packageName;
  const activityName = compiler.options.activityName;

  // Detect XR-specific permissions based on composition traits
  const hasHandTracking = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'hand_tracking')
  );
  const hasFaceTracking = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'face_tracking')
  );
  const hasHeadFollow = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'follows_head' || t.name === 'head_follow')
  );
  const hasEyeTracking = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'eye_tracking' || t.name === 'eye_tracked')
  );
  const hasPlaneDetection = composition.objects?.some((o) =>
    o.traits?.some((t) => t.name === 'plane_detection' || t.name === 'anchor')
  );

  const xrPermissions: string[] = [];
  xrPermissions.push('    <uses-permission android:name="android.permission.CAMERA" />');
  if (hasHandTracking) {
    xrPermissions.push('    <uses-permission android:name="android.permission.HAND_TRACKING" />');
  }
  if (hasFaceTracking) {
    xrPermissions.push('    <uses-permission android:name="android.permission.FACE_TRACKING" />');
  }
  if (hasHeadFollow) {
    xrPermissions.push('    <uses-permission android:name="android.permission.HEAD_TRACKING" />');
  }
  if (hasEyeTracking) {
    xrPermissions.push('    <uses-permission android:name="android.permission.EYE_TRACKING" />');
  }
  if (hasPlaneDetection) {
    xrPermissions.push(
      '    <uses-permission android:name="android.permission.SCENE_UNDERSTANDING_COARSE" />'
    );
  }

  const needsArCameraFeature =
    compiler.options.useARCore && compiler.compositionUsesArCoreDepthTraits(composition);
  const arRequired = compiler.options.arCameraHardwareRequired ? 'true' : 'false';
  const arCameraFeatureLine = needsArCameraFeature
    ? `    <uses-feature android:name="android.hardware.camera.ar" android:required="${arRequired}" />`
    : '';

  return `<?xml version="1.0" encoding="utf-8"?>
<!-- Auto-generated by HoloScript AndroidXRCompiler -->
<manifest xmlns:android="http://schemas.android.com/apk/res/android"
    package="${pkg}">

    <!-- XR Permissions -->
${xrPermissions.join('\n')}

    <!-- XR Required Features -->
    <uses-feature android:name="android.hardware.xr.headtracking" android:required="true" />
    <uses-feature android:glEsVersion="0x00030002" android:required="true" />
${arCameraFeatureLine ? `${arCameraFeatureLine}\n` : ''}
    <application
        android:allowBackup="true"
        android:label="${compiler.escapeStringValue(composition.name as string, 'XML')}"
        android:supportsRtl="true"
        android:theme="@style/Theme.MaterialComponents.DayNight.NoActionBar">

        <!-- Android XR metadata -->
        <meta-data android:name="com.google.ar.core" android:value="required" />

        <activity
            android:name=".${activityName}"
            android:exported="true"
            android:configChanges="orientation|screenSize|keyboardHidden|screenLayout|uiMode"
            android:screenOrientation="landscape"
            android:enableOnBackInvokedCallback="true">
            <intent-filter>
                <action android:name="android.intent.action.MAIN" />
                <category android:name="android.intent.category.LAUNCHER" />
                <category android:name="com.google.intent.category.XR" />
            </intent-filter>
        </activity>
    </application>
</manifest>`;
}

