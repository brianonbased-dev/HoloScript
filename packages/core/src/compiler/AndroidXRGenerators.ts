// ─── Timeline ────────────────────────────────────────────────────────

export function emitTimeline(compiler: AndroidXRCompiler, tl: HoloTimeline): void {
  const fn = compiler.sanitizeName(tl.name);
  compiler.emit('');
  compiler.emit(`private fun playTimeline_${fn}() {`);
  compiler.indent();
  compiler.emit('val set = AnimatorSet()');
  compiler.emit('val anims = mutableListOf<ObjectAnimator>()');

  for (const e of tl.entries) {
    const ms = Math.round(e.time * 1000);
    if (e.action.kind === 'animate') {
      for (const [k, val] of Object.entries(e.action.properties)) {
        if (typeof val === 'number') {
          compiler.emit(
            `anims.add(ObjectAnimator.ofFloat(null, "${k}", ${val}f).apply { startDelay = ${ms}L; duration = 300L })`
          );
        }
      }
    } else if (e.action.kind === 'emit') {
      compiler.emit(`// @${e.time}s emit "${e.action.event}"`);
    } else if (e.action.kind === 'call') {
      compiler.emit(`// @${e.time}s call ${e.action.method}()`);
    }
  }

  compiler.emit('set.playTogether(anims as List<android.animation.Animator>)');
  if (tl.loop) {
    compiler.emit('set.addListener(object : android.animation.AnimatorListenerAdapter() {');
    compiler.indent();
    compiler.emit(
      `override fun onAnimationEnd(a: android.animation.Animator) { playTimeline_${fn}() }`
    );
    compiler.dedent();
    compiler.emit('})');
  }
  compiler.emit('set.start()');
  compiler.dedent();
  compiler.emit('}');
}

// ─── Audio ───────────────────────────────────────────────────────────

export function emitAudio(compiler: AndroidXRCompiler, audio: HoloAudio): void {
  const v = compiler.sanitizeName(audio.name);
  const src = audio.properties.find((p) => p.key === 'src' || p.key === 'source')?.value;
  const spatial = audio.properties.find((p) => p.key === 'spatial')?.value;
  const volume = audio.properties.find((p) => p.key === 'volume')?.value ?? 1.0;
  const loop = audio.properties.find((p) => p.key === 'loop')?.value;
  const pos = audio.properties.find((p) => p.key === 'position')?.value;

  compiler.emit('');
  compiler.emit(`// Audio: ${compiler.escapeStringValue(audio.name as string, 'Kotlin')}`);
  if (src) {
    compiler.emit(`val ${v}Id = soundPool.load(this, R.raw.${compiler.sanitizeName(src as string)}, 1)`);
  }
  if (spatial && pos && Array.isArray(pos)) {
    compiler.emit(
      `val ${v}Entity = xrSession.scene.createEntity("${compiler.escapeStringValue(audio.name as string, 'Kotlin')}")`
    );
    compiler.emit(
      `${v}Entity.setPose(Pose(${compiler.toKotlinFloat3(pos as number[])}, Quaternion.identity()))`
    );
    compiler.emit(`val ${v}Track = SpatialAudioTrack(xrSession, ${v}Entity)`);
  }
  if (src) {
    compiler.emit(`soundPool.setOnLoadCompleteListener { pool, id, _ ->`);
    compiler.indent();
    compiler.emit(
      `if (id == ${v}Id) pool.play(id, ${volume}f, ${volume}f, 1, ${loop ? 1 : 0}, 1.0f)`
    );
    compiler.dedent();
    compiler.emit('}');
  }
}

