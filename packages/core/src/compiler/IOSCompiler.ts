    this.emit('print("[HoloScript] Failed to access front camera")');
    this.emit('return');
    this.indentLevel--;
    this.emit('}');
    this.emit('');
    this.emit('session.addInput(input)');
    this.emit('');
    this.emit('let output = AVCaptureVideoDataOutput()');
    this.emit('output.setSampleBufferDelegate(self, queue: DispatchQueue(label: "handTracking"))');
