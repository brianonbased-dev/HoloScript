    this.emit('');

    // processObservation
    this.emit('private func processObservation(_ observation: VNHumanHandPoseObservation) {');
    this.indentLevel++;
    this.emit('guard let thumbTip = try? observation.recognizedPoint(.thumbTip),');
    this.emit('      let indexTip = try? observation.recognizedPoint(.indexTip),');
    this.emit('      let indexMCP = try? observation.recognizedPoint(.indexMCP),');
    this.emit('      let middleTip = try? observation.recognizedPoint(.middleTip),');
    this.emit('      let middleMCP = try? observation.recognizedPoint(.middleMCP),');
    this.emit('      let middlePIP = try? observation.recognizedPoint(.middlePIP),');
    this.emit('      let ringTip = try? observation.recognizedPoint(.ringTip),');
    this.emit('      let ringMCP = try? observation.recognizedPoint(.ringMCP),');
    this.emit('      let ringPIP = try? observation.recognizedPoint(.ringPIP),');
    this.emit('      let littleTip = try? observation.recognizedPoint(.littleTip),');
    this.emit('      let littleMCP = try? observation.recognizedPoint(.littleMCP),');
    this.emit('      let littlePIP = try? observation.recognizedPoint(.littlePIP),');
    this.emit('      let indexPIP = try? observation.recognizedPoint(.indexPIP)');
