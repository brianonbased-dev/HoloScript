      this.emit('switch shape {');
      this.emit('case "circle":');
      this.indentLevel++;
      this.emit('let torus = SCNTorus(ringRadius: CGFloat(radius), pipeRadius: 0.02)');
      this.emit('torus.firstMaterial?.diffuse.contents = UIColor.cyan.withAlphaComponent(0.8)');
      this.emit('boundaryNode.geometry = torus');
      this.indentLevel--;
      this.emit('case "rect":');
      this.indentLevel++;
