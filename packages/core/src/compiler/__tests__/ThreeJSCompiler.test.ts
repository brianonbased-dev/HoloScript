import { describe, it, expect, vi, beforeEach } from 'vitest';
import { ThreeJSCompiler } from '../ThreeJSCompiler';
import type { HoloComposition } from '../../parser/HoloCompositionTypes';

vi.mock('../identity/AgentRBAC', async (importOriginal) => {
  const actual = await importOriginal();
  return {
    ...actual,
    getRBAC: () => ({ checkAccess: () => ({ allowed: true }) }),
  };
});

function makeComposition(overrides: Partial<HoloComposition> = {}): HoloComposition {
  return {
    type: 'Composition',
    name: 'TestScene',
    objects: [],
    spatialGroups: [],
    ...overrides,
  } as HoloComposition;
}

describe('ThreeJSCompiler', () => {
  let compiler: ThreeJSCompiler;

  beforeEach(() => {
    compiler = new ThreeJSCompiler();
  });

  it('compiles a minimal composition to Three.js code', () => {
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('import * as THREE from "three"');
    expect(output).toContain('new THREE.Scene()');
    expect(output).toContain('new THREE.WebGLRenderer');
    expect(output).toContain('export class GeneratedScene');
    expect(output).toContain('async build(): Promise<void>');
    expect(output).toContain('dispose(): void');
  });

  it('emits correct header comments', () => {
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('// Auto-generated by HoloScript ThreeJSCompiler');
    expect(output).toContain('// Source: composition "TestScene"');
  });

  it('emits provenance hash when provided', () => {
    const c = new ThreeJSCompiler({ provenanceHash: 'abc123' });
    const output = c.compile(makeComposition(), 'test-token');
    expect(output).toContain('// Provenance Hash: abc123');
  });

  it('uses custom className', () => {
    const c = new ThreeJSCompiler({ className: 'MyScene' });
    const output = c.compile(makeComposition(), 'test-token');
    expect(output).toContain('export class MyScene');
  });

  it('emits shadow map configuration when enableShadows=true (default)', () => {
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('shadowMap.enabled = true');
  });

  it('omits shadow map when enableShadows=false', () => {
    const c = new ThreeJSCompiler({ enableShadows: false });
    const output = c.compile(makeComposition(), 'test-token');
    expect(output).not.toContain('shadowMap.enabled');
  });

  it('emits default lighting when no lights defined', () => {
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('new THREE.AmbientLight');
    expect(output).toContain('new THREE.DirectionalLight');
  });

  it('emits ambient and directional lights from composition', () => {
    const lights: HoloComposition['lights'] = [
      {
        type: 'Light',
        name: 'sun',
        lightType: 'directional',
        properties: [
          { type: 'LightProperty', key: 'intensity', value: 1.5 },
          { type: 'LightProperty', key: 'position', value: [10, 20, 10] },
        ],
      },
    ];
    const output = compiler.compile(makeComposition({ lights }), 'test-token');
    expect(output).toContain('new THREE.DirectionalLight');
    expect(output).toContain('1.5');
    expect(output).toContain('position.set(10, 20, 10)');
  });

  it('emits a BoxGeometry for cube objects', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'myBox',
        traits: [],
        properties: [
          { type: 'Property', key: 'geometry', value: 'cube' },
          { type: 'Property', key: 'color', value: '#ff0000' },
          { type: 'Property', key: 'position', value: [0, 1, 0] },
        ],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('new THREE.BoxGeometry');
    expect(output).toContain('0xff0000');
    expect(output).toContain('myBox.position.set(0, 1, 0)');
  });

  it('emits a SphereGeometry for sphere objects', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'ball',
        traits: [],
        properties: [{ type: 'Property', key: 'geometry', value: 'sphere' }],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('new THREE.SphereGeometry');
  });

  it('emits scale for objects with numeric scale', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'bigBox',
        traits: [],
        properties: [
          { type: 'Property', key: 'geometry', value: 'cube' },
          { type: 'Property', key: 'scale', value: 2 },
        ],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('bigBox.scale.setScalar(2)');
  });

  it('emits scale.set for objects with array scale', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'scaledBox',
        traits: [],
        properties: [
          { type: 'Property', key: 'geometry', value: 'cube' },
          { type: 'Property', key: 'scale', value: [1, 2, 3] },
        ],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('scaledBox.scale.set(1, 2, 3)');
  });

  it('emits a spatial group with children', () => {
    const spatialGroups: HoloComposition['spatialGroups'] = [
      {
        type: 'SpatialGroup',
        name: 'room',
        properties: [{ type: 'SpatialGroupProperty', key: 'position', value: [0, 0, -5] }],
        objects: [
          {
            type: 'ObjectDecl',
            name: 'table',
            traits: [],
            properties: [{ type: 'Property', key: 'geometry', value: 'cube' }],
          },
        ],
      },
    ];
    const output = compiler.compile(makeComposition({ spatialGroups }), 'test-token');
    expect(output).toContain('new THREE.Group()');
    expect(output).toContain('room.position.set(0, 0, -5)');
    expect(output).toContain('this.scene.add(room)');
    expect(output).toContain('room.add(table)');
  });

  it('emits post-processing imports when enabled', () => {
    const c = new ThreeJSCompiler({ enablePostProcessing: true });
    const output = c.compile(makeComposition(), 'test-token');
    expect(output).toContain('EffectComposer');
    expect(output).toContain('UnrealBloomPass');
  });

  it('emits render loop and resize handler', () => {
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('requestAnimationFrame');
    expect(output).toContain('this.renderer.render(this.scene, this.camera)');
    expect(output).toContain('window.addEventListener("resize"');
  });

  it('emits camera settings from composition', () => {
    const camera: HoloComposition['camera'] = {
      type: 'Camera',
      cameraType: 'perspective',
      properties: [
        { type: 'CameraProperty', key: 'fov', value: 60 },
        { type: 'CameraProperty', key: 'position', value: [0, 5, 20] },
        { type: 'CameraProperty', key: 'target', value: [0, 0, 0] },
      ],
    };
    const output = compiler.compile(makeComposition({ camera }), 'test-token');
    expect(output).toContain('this.camera.fov = 60');
    expect(output).toContain('this.camera.position.set(0, 5, 20)');
    expect(output).toContain('this.camera.lookAt');
  });

  it('emits environment background color', () => {
    const environment: HoloComposition['environment'] = {
      type: 'Environment',
      properties: [{ type: 'EnvironmentProperty', key: 'background', value: '#1a1a2e' }],
    };
    const output = compiler.compile(makeComposition({ environment }), 'test-token');
    expect(output).toContain('new THREE.Color(0x1a1a2e)');
  });

  it('emits physics comment for collidable trait', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'physBox',
        traits: [{ name: 'collidable', config: {} }],
        properties: [{ type: 'Property', key: 'geometry', value: 'cube' }],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('@physics');
  });

  it('emits grabbable comment for grabbable trait', () => {
    const objects: HoloComposition['objects'] = [
      {
        type: 'ObjectDecl',
        name: 'grabObj',
        traits: [{ name: 'grabbable', config: {} }],
        properties: [{ type: 'Property', key: 'geometry', value: 'cube' }],
      },
    ];
    const output = compiler.compile(makeComposition({ objects }), 'test-token');
    expect(output).toContain('@grabbable');
  });

  it('compilerName is ThreeJSCompiler', () => {
    // Access via any to test the protected property indirectly via compile output
    const output = compiler.compile(makeComposition(), 'test-token');
    expect(output).toContain('ThreeJSCompiler');
  });
});
