import { describe, it, expect } from 'vitest';
import {
  compilePipelineSource,
  compilePipelineSourceToNode,
  compilePipelineSourceToPython,
} from '../PipelineNodeCompiler';

const SOURCE = `
  pipeline "InventorySync" {
    source POS {
      type: "list"
    }

    transform MapFields {
      sku -> productId
      qty -> stock : multiply(100)
    }

    filter Active {
      where: stock > 0
    }

    validate Product {
      productId: required, string
      stock: required
    }

    sink Out {
      type: "stdout"
    }
  }
`;

const UNSUPPORTED_SOURCE = `
  pipeline "UnsupportedSource" {
    source DB {
      type: "database"
    }

    sink Out {
      type: "stdout"
    }
  }
`;

const UNSUPPORTED_SINK = `
  pipeline "UnsupportedSink" {
    source Items {
      type: "list"
    }

    sink Scene {
      type: "holo"
    }
  }
`;

describe('PipelineNodeCompiler', () => {
  it('compiles pipeline source into runnable node module code', () => {
    const result = compilePipelineSourceToNode(SOURCE, { moduleName: 'index.mjs' });
    expect(result.success).toBe(true);
    expect(result.code).toContain('export async function runPipeline()');
    expect(result.code).toContain('applyTransforms');
    expect(result.code).toContain('Pipeline completed');
  });

  it('returns parser errors for invalid pipeline source', () => {
    const result = compilePipelineSourceToNode('object "Cube" { }');
    expect(result.success).toBe(false);
    expect(result.errors?.length).toBeGreaterThan(0);
  });

  it('compiles pipeline source into runnable python module code', () => {
    const result = compilePipelineSourceToPython(SOURCE, { moduleName: 'pipeline.py' });
    expect(result.success).toBe(true);
    expect(result.code).toContain('async def run_pipeline()');
    expect(result.code).toContain('AsyncIOScheduler');
    expect(result.code).toContain('CronTrigger');
  });

  it('selects python target with target-aware compile API', () => {
    const result = compilePipelineSource(SOURCE, {
      target: 'python',
      python: { moduleName: 'pipeline.py' },
    });

    expect(result.success).toBe(true);
    expect(result.code).toContain('Generated by HoloScript PipelinePythonCompiler');
  });

  it('defaults target-aware compile API to node target', () => {
    const result = compilePipelineSource(SOURCE, { node: { moduleName: 'index.mjs' } });

    expect(result.success).toBe(true);
    expect(result.code).toContain('Generated by HoloScript PipelineNodeCompiler');
  });

  it('emits runtime throws for unsupported node source and sink types', () => {
    const sourceResult = compilePipelineSourceToNode(UNSUPPORTED_SOURCE);
    expect(sourceResult.success).toBe(true);
    expect(sourceResult.code).toContain(
      '[PipelineNodeCompiler] Unsupported pipeline source type \\"database\\" (step: DB)'
    );
    expect(sourceResult.code).not.toContain('datasets["DB"] = [];');

    const sinkResult = compilePipelineSourceToNode(UNSUPPORTED_SINK);
    expect(sinkResult.success).toBe(true);
    expect(sinkResult.code).toContain(
      '[PipelineNodeCompiler] Unsupported pipeline sink type \\"holo\\" (step: Scene)'
    );
    expect(sinkResult.code).not.toContain('not implemented');
  });

  it('emits runtime raises for unsupported python source and sink types', () => {
    const sourceResult = compilePipelineSourceToPython(UNSUPPORTED_SOURCE);
    expect(sourceResult.success).toBe(true);
    expect(sourceResult.code).toContain('raise NotImplementedError');
    expect(sourceResult.code).toContain(
      '[PipelineNodeCompiler] Unsupported pipeline source type \\"database\\" (step: DB)'
    );
    expect(sourceResult.code).not.toContain('datasets["DB"] = []');

    const sinkResult = compilePipelineSourceToPython(UNSUPPORTED_SINK);
    expect(sinkResult.success).toBe(true);
    expect(sinkResult.code).toContain('raise NotImplementedError');
    expect(sinkResult.code).toContain(
      '[PipelineNodeCompiler] Unsupported pipeline sink type \\"holo\\" (step: Scene)'
    );
    expect(sinkResult.code).not.toContain('# sink Scene (holo) not implemented');
  });
});
