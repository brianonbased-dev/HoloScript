/**
 * USDZPipeline — Production Test Suite
 *
 * Covers: USDA generation, header/stage metadata, material collection,
 * geometry generation (sphere, cube, cylinder, cone, plane, text),
 * sanitizeName, hexToRGB, export helpers, pipeline options.
 */
import { describe, it, expect } from 'vitest';
import {
  USDZPipeline,
  generateUSDA,
  getUSDZConversionCommand,
  getPythonConversionScript,
} from '../USDZPipeline';
import type { HoloComposition } from '../../parser/HoloCompositionTypes';

// ─── Helpers ────────────────────────────────────────────────────────
function makeComposition(overrides: Partial<HoloComposition> = {}): HoloComposition {
  return {
    name: 'TestScene',
    objects: [
      {
        name: 'Box1',
        traits: [],
        properties: [
          { key: 'mesh', value: 'cube' },
          { key: 'position', value: [1, 2, 3] },
        ],
      },
    ],
    spatialGroups: [],
    ...overrides,
  } as HoloComposition;
}

describe('USDZPipeline — Production', () => {
  // ─── Construction ─────────────────────────────────────────────────
  it('uses default options when none provided', () => {
    const pipeline = new USDZPipeline();
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).toContain('#usda 1.0');
    expect(usda).toContain('upAxis = "Y"');
    expect(usda).toContain('metersPerUnit = 1');
  });

  it('respects custom up axis', () => {
    const pipeline = new USDZPipeline({ upAxis: 'Z' });
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).toContain('upAxis = "Z"');
  });

  it('respects custom metersPerUnit', () => {
    const pipeline = new USDZPipeline({ metersPerUnit: 0.01 });
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).toContain('metersPerUnit = 0.01');
  });

  // ─── Header ───────────────────────────────────────────────────────
  it('header includes HoloScript doc tag', () => {
    const pipeline = new USDZPipeline();
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).toContain('Generated by HoloScript USDZPipeline');
  });

  it('header uses sanitized defaultPrim', () => {
    const pipeline = new USDZPipeline();
    const usda = pipeline.generateUSDA(makeComposition({ name: 'My Scene!@#' }));
    expect(usda).toContain('defaultPrim = "My_Scene___"');
  });

  // ─── Materials ────────────────────────────────────────────────────
  it('generates default material', () => {
    const pipeline = new USDZPipeline();
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).toContain('def Material "DefaultMaterial"');
    expect(usda).toContain('UsdPreviewSurface');
  });

  it('collects color material from object', () => {
    const comp = makeComposition({
      objects: [
        {
          name: 'Red',
          traits: [],
          properties: [{ key: 'color', value: '#ff0000' }],
        },
      ],
    });
    const pipeline = new USDZPipeline();
    const usda = pipeline.generateUSDA(comp);
    expect(usda).toContain('def Material "Material_Red"');
  });

  it('skips materials when exportMaterials is false', () => {
    const pipeline = new USDZPipeline({ exportMaterials: false });
    const usda = pipeline.generateUSDA(makeComposition());
    expect(usda).not.toContain('def Material');
  });

  // ─── Geometry ─────────────────────────────────────────────────────
  it('generates Sphere geometry', () => {
    const comp = makeComposition({
      objects: [
        {
          name: 'Ball',
          traits: [],
          properties: [
            { key: 'mesh', value: 'sphere' },
            { key: 'radius', value: 2 },
          ],
        },
      ],
    });
    const usda = generateUSDA(comp);
    expect(usda).toContain('def Sphere "Geometry"');
    expect(usda).toContain('double radius = 2');
  });

  it('generates Cube geometry', () => {
    const usda = generateUSDA(makeComposition());
    expect(usda).toContain('def Cube "Geometry"');
  });

  it('generates Cylinder geometry', () => {
    const comp = makeComposition({
      objects: [{ name: 'Cyl', traits: [], properties: [{ key: 'mesh', value: 'cylinder' }] }],
    });
    const usda = generateUSDA(comp);
    expect(usda).toContain('def Cylinder "Geometry"');
  });

  it('generates Plane geometry', () => {
    const comp = makeComposition({
      objects: [{ name: 'Floor', traits: [], properties: [{ key: 'mesh', value: 'plane' }] }],
    });
    const usda = generateUSDA(comp);
    expect(usda).toContain('def Mesh "Geometry"');
    expect(usda).toContain('faceVertexCounts');
  });

  // ─── Transforms ───────────────────────────────────────────────────
  it('includes translation from position', () => {
    const usda = generateUSDA(makeComposition());
    expect(usda).toContain('xformOp:translate = (1, 2, 3)');
  });

  it('includes rotation and scale ops', () => {
    const comp = makeComposition({
      objects: [
        {
          name: 'T',
          traits: [],
          properties: [
            { key: 'mesh', value: 'cube' },
            { key: 'rotation', value: [45, 0, 90] },
            { key: 'scale', value: [2, 2, 2] },
          ],
        },
      ],
    });
    const usda = generateUSDA(comp);
    expect(usda).toContain('xformOp:rotateXYZ');
    expect(usda).toContain('xformOp:scale');
  });

  // ─── Export Functions ─────────────────────────────────────────────
  it('getUSDZConversionCommand returns xcrun command', () => {
    const cmd = getUSDZConversionCommand('input.usda', 'output.usdz');
    expect(cmd).toContain('xcrun usdz_converter');
    expect(cmd).toContain('input.usda');
    expect(cmd).toContain('output.usdz');
  });

  it('getPythonConversionScript returns Python code', () => {
    const script = getPythonConversionScript('input.usda', 'output.usdz');
    expect(script).toContain('from pxr import Usd');
    expect(script).toContain('CreateNewUsdzPackage');
  });

  // ─── Scene Hierarchy ──────────────────────────────────────────────
  it('generates root Xform with assembly kind', () => {
    const usda = generateUSDA(makeComposition());
    expect(usda).toContain('kind = "assembly"');
  });
});
