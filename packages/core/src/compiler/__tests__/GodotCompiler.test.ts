import { describe, it, expect, beforeEach, vi} from 'vitest';
import { GodotCompiler } from '../GodotCompiler';
import type { HoloComposition } from '../../parser/HoloCompositionTypes';

vi.mock('../identity/AgentRBAC', async (importOriginal) => {
  const actual = await importOriginal();
  return {
    ...actual,
    getRBAC: () => ({ checkAccess: () => ({ allowed: true }) }),
  };
});


function makeComposition(overrides: Partial<HoloComposition> = {}): HoloComposition {
  return { name: 'TestScene', objects: [], ...overrides } as HoloComposition;
}

describe('GodotCompiler', () => {
  let compiler: GodotCompiler;

  beforeEach(() => {
    compiler = new GodotCompiler();
  });

  // =========== Constructor / defaults ===========

  it('compiles minimal composition to GDScript', () => {
    const gd = compiler.compile(makeComposition(), 'test-token');
    expect(gd).toContain('extends Node3D');
    expect(gd).toContain('func _ready()');
  });

  it('includes auto-generated header comment', () => {
    const gd = compiler.compile(makeComposition(), 'test-token');
    expect(gd).toContain('Auto-generated by HoloScript');
    expect(gd).toContain('TestScene');
  });

  it('respects custom className', () => {
    const c = new GodotCompiler({ className: 'MyClass' });
    const gd = c.compile(makeComposition(), 'test-token');
    expect(gd).toContain('class_name MyClass');
  });

  it('targets godot version in header', () => {
    const c = new GodotCompiler({ godotVersion: '4.2' });
    const gd = c.compile(makeComposition(), 'test-token');
    expect(gd).toContain('Godot 4.2');
  });

  // =========== State → variables ===========

  it('compiles state properties to GDScript vars', () => {
    const comp = makeComposition({
      state: {
        properties: [
          { key: 'health', value: 100 },
          { key: 'name', value: 'Player' },
          { key: 'active', value: true },
        ],
      },
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('var health = 100');
    expect(gd).toContain('var active = true');
  });

  // =========== Objects ===========

  it('compiles objects into _ready body', () => {
    const comp = makeComposition({
      objects: [
        {
          name: 'cube',
          properties: [
            { key: 'geometry', value: 'box' },
            { key: 'position', value: [1, 2, 3] },
          ],
          traits: [],
        },
      ] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('cube');
    expect(gd).toContain('add_child');
  });

  it('handles sphere geometry', () => {
    const comp = makeComposition({
      objects: [
        {
          name: 'ball',
          properties: [{ key: 'geometry', value: 'sphere' }],
          traits: [],
        },
      ] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('ball');
  });

  // =========== Lights ===========

  it('compiles lights', () => {
    const comp = makeComposition({
      lights: [{ name: 'sun', lightType: 'directional', properties: [{ key: 'color', value: '#ffffff' }] }] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('DirectionalLight3D');
  });

  // =========== Camera ===========

  it('compiles camera', () => {
    const comp = makeComposition({
      camera: { position: [0, 2, 5], target: [0, 0, 0] } as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('Camera');
  });

  // =========== Environment ===========

  it('compiles environment settings', () => {
    const comp = makeComposition({
      environment: { properties: [{ key: 'skybox', value: 'sunset' }] } as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('Environment');
  });

  // =========== Spatial groups ===========

  it('compiles spatial groups', () => {
    const comp = makeComposition({
      spatialGroups: [
        {
          name: 'group1',
          objects: [
            { name: 'item', properties: [{ key: 'geometry', value: 'box' }], traits: [] },
          ],
          properties: [],
        },
      ] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('group1');
  });

  // =========== Audio ===========

  it('compiles audio nodes', () => {
    const comp = makeComposition({
      audio: [{ name: 'bgm', properties: [{ key: 'src', value: 'music.ogg' }, { key: 'loop', value: true }] }] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('AudioStreamPlayer');
  });

  // =========== Multiple objects ===========

  it('compiles multiple objects', () => {
    const comp = makeComposition({
      objects: [
        { name: 'obj_a', properties: [{ key: 'geometry', value: 'box' }], traits: [] },
        { name: 'obj_b', properties: [{ key: 'geometry', value: 'sphere' }], traits: [] },
      ] as any,
    });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('obj_a');
    expect(gd).toContain('obj_b');
  });

  // =========== Name sanitization ===========

  it('sanitizes special characters in names', () => {
    const comp = makeComposition({ name: 'my scene!' });
    const gd = compiler.compile(comp, 'test-token');
    expect(gd).toContain('my scene!');
    // At least the objects/vars use sanitized names
  });
});
