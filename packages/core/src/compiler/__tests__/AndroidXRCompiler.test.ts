import { describe, it, expect, beforeEach } from 'vitest';
import { AndroidXRCompiler } from '../AndroidXRCompiler';

function minimalComposition(overrides: any = {}) {
  return {
    name: 'TestScene',
    objects: [],
    lights: [],
    spatialGroups: [],
    audio: [],
    timelines: [],
    transitions: [],
    zones: [],
    ...overrides,
  };
}

describe('AndroidXRCompiler', () => {
  let compiler: AndroidXRCompiler;

  beforeEach(() => {
    compiler = new AndroidXRCompiler({ packageName: 'com.test.app', activityName: 'TestActivity' });
  });

  it('emits package declaration', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('package com.test.app');
  });

  it('emits activity class name', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('class TestActivity : ComponentActivity()');
  });

  it('emits auto-generated header comment', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('Auto-generated by HoloScript AndroidXRCompiler');
  });

  it('emits Compose Scene function', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('TestSceneScene()');
  });

  it('emits Subspace block', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('Subspace {');
  });

  it('emits ARCore imports when useARCore is true', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('com.google.ar.core.Config');
  });

  it('omits ARCore imports when useARCore is false', () => {
    compiler = new AndroidXRCompiler({ useARCore: false });
    const out = compiler.compile(minimalComposition());
    expect(out).not.toContain('com.google.ar.core.Config');
  });

  it('emits Filament imports when useFilament is true', () => {
    const out = compiler.compile(minimalComposition());
    expect(out).toContain('com.google.android.filament');
  });

  it('compiles object with position', () => {
    const comp = minimalComposition({
      objects: [{ name: 'myBox', properties: [{ key: 'position', value: [1, 2, 3] }, { key: 'mesh', value: 'box' }], traits: [], children: [] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('Float3(1f, 2f, 3f)');
    expect(out).toContain('myBox');
  });

  it('compiles light with type and intensity', () => {
    const comp = minimalComposition({
      lights: [{ name: 'sun', lightType: 'directional', properties: [{ key: 'intensity', value: 1.5 }] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('LightManager.Type.DIRECTIONAL');
    expect(out).toContain('Intensity: 150000f lux');
  });

  it('compiles audio with SoundPool', () => {
    const comp = minimalComposition({
      audio: [{ name: 'bgMusic', properties: [{ key: 'src', value: 'bgm' }, { key: 'loop', value: true }] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('SoundPool');
    expect(out).toContain('bgMusic');
  });

  it('sanitizes names with special characters', () => {
    const comp = minimalComposition({
      objects: [{ name: 'my-special.box!', properties: [{ key: 'mesh', value: 'box' }], traits: [], children: [] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('my_special_box_');
  });

  it('compiles state with mutableStateOf', () => {
    const comp = minimalComposition({
      state: { properties: [{ key: 'count', value: 0 }, { key: 'label', value: 'hello' }] },
    });
    const out = compiler.compile(comp);
    expect(out).toContain('mutableStateOf(0)');
    expect(out).toContain('mutableStateOf("hello")');
  });

  it('compiles hand tracking trait imports', () => {
    const comp = minimalComposition({
      objects: [{ name: 'hand', properties: [], traits: [{ name: 'hand_tracking' }], children: [] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('HandNode');
  });

  it('compiles timeline with animation', () => {
    const comp = minimalComposition({
      timelines: [{ name: 'intro', loop: true, entries: [{ time: 0, action: { kind: 'animate', properties: { opacity: 1 } } }] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('playTimeline_intro');
    expect(out).toContain('AnimatorSet');
  });

  it('compiles transition', () => {
    const comp = minimalComposition({
      transitions: [{ name: 'fadeOut', properties: [{ key: 'target', value: 'scene2' }, { key: 'duration', value: 1.0 }] }],
    });
    const out = compiler.compile(comp);
    expect(out).toContain('transition_fadeOut');
    expect(out).toContain('1000L');
  });
});
