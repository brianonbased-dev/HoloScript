import { describe, it, expect, beforeEach, vi } from 'vitest';
import { AndroidXRCompiler } from '../AndroidXRCompiler';
import type { AndroidXRCompileResult } from '../AndroidXRCompiler';

vi.mock('../identity/AgentRBAC', async (importOriginal) => {
  const actual = await importOriginal();
  return {
    ...actual,
    getRBAC: () => ({ checkAccess: () => ({ allowed: true }) }),
  };
});

function minimalComposition(overrides: any = {}) {
  return {
    name: 'TestScene',
    objects: [],
    lights: [],
    spatialGroups: [],
    audio: [],
    timelines: [],
    transitions: [],
    zones: [],
    ...overrides,
  };
}

describe('AndroidXRCompiler', () => {
  let compiler: AndroidXRCompiler;

  beforeEach(() => {
    compiler = new AndroidXRCompiler({ packageName: 'com.test.app', activityName: 'TestActivity' });
  });

  // ─── Result structure ─────────────────────────────────────────────

  it('returns AndroidXRCompileResult with all files', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result).toHaveProperty('activityFile');
    expect(result).toHaveProperty('stateFile');
    expect(result).toHaveProperty('nodeFactoryFile');
    expect(result).toHaveProperty('manifestFile');
    expect(result).toHaveProperty('buildGradle');
  });

  // ─── Activity file ────────────────────────────────────────────────

  it('emits package declaration', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('package com.test.app');
  });

  it('emits activity class name', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('class TestActivity : ComponentActivity()');
  });

  it('emits auto-generated header comment', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('Auto-generated by HoloScript AndroidXRCompiler');
  });

  it('emits provenance hash in activity when provenanceHash is set', () => {
    const h = 'abc123provenance';
    const c = new AndroidXRCompiler({
      packageName: 'com.test.app',
      activityName: 'TestActivity',
      provenanceHash: h,
    });
    const result = c.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain(`// Provenance Hash: ${h}`);
  });

  it('emits Compose Scene function', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('TestSceneScene()');
  });

  it('emits Subspace block', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('Subspace {');
  });

  it('emits ARCore imports when useARCore is true', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('com.google.ar.core.Config');
  });

  it('omits ARCore imports when useARCore is false', () => {
    compiler = new AndroidXRCompiler({ useARCore: false });
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).not.toContain('com.google.ar.core.Config');
  });

  it('emits Filament imports when useFilament is true', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('com.google.android.filament');
  });

  it('compiles object with position', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'myBox',
          properties: [
            { key: 'position', value: [1, 2, 3] },
            { key: 'mesh', value: 'box' },
          ],
          traits: [],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('Float3(1f, 2f, 3f)');
    expect(result.activityFile).toContain('myBox');
  });

  it('compiles light with type and intensity', () => {
    const comp = minimalComposition({
      lights: [
        { name: 'sun', lightType: 'directional', properties: [{ key: 'intensity', value: 1.5 }] },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('LightManager.Type.DIRECTIONAL');
    expect(result.activityFile).toContain('Intensity: 150000f lux');
  });

  it('compiles audio with SoundPool', () => {
    const comp = minimalComposition({
      audio: [
        {
          name: 'bgMusic',
          properties: [
            { key: 'src', value: 'bgm' },
            { key: 'loop', value: true },
          ],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('SoundPool');
    expect(result.activityFile).toContain('bgMusic');
  });

  it('sanitizes names with special characters', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'my-special.box!',
          properties: [{ key: 'mesh', value: 'box' }],
          traits: [],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('my_special_box_');
  });

  it('compiles state with mutableStateOf', () => {
    const comp = minimalComposition({
      state: {
        properties: [
          { key: 'count', value: 0 },
          { key: 'label', value: 'hello' },
        ],
      },
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('mutableStateOf(0)');
    expect(result.activityFile).toContain('mutableStateOf("hello")');
  });

  it('compiles hand tracking trait imports', () => {
    const comp = minimalComposition({
      objects: [
        { name: 'hand', properties: [], traits: [{ name: 'hand_tracking' }], children: [] },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('InputEvent');
  });

  // ─── DP3: URI-based 3D model loading ──────────────────────────────

  it('compiles model object with URI-based GltfModel (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'myModel',
          properties: [
            { key: 'model', value: 'models/robot.glb' },
            { key: 'position', value: [0, 1, -2] },
          ],
          traits: [],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain(
      'GltfModel.create(xrSession, Uri.parse("models/robot.glb"))'
    );
    expect(result.activityFile).toContain('GltfModelEntity.create(xrSession');
    expect(result.activityFile).not.toContain('ArModelNode');
  });

  // ─── DP3: Face tracking with 68 blendshapes ──────────────────────

  it('emits face tracking imports when face_tracking trait is present (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'avatar',
          properties: [],
          traits: [{ name: 'face_tracking', config: {} }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('FaceBlendShapeType');
    expect(result.activityFile).toContain('FaceTrackingMode');
    expect(result.activityFile).toContain('Face.getUserFace');
  });

  it('emits specific blendshape reads when configured (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'avatar',
          properties: [],
          traits: [
            { name: 'face_tracking', config: { blendshapes: ['JAW_DROP', 'EYES_CLOSED_L'] } },
          ],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('FACE_BLEND_SHAPE_TYPE_JAW_DROP');
    expect(result.activityFile).toContain('FACE_BLEND_SHAPE_TYPE_EYES_CLOSED_L');
  });

  // ─── DP3: UserSubspace head-following UI ──────────────────────────

  it('emits UserSubspace import for follows_head trait (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'hud',
          properties: [
            { key: 'width', value: 500 },
            { key: 'height', value: 300 },
          ],
          traits: [{ name: 'follows_head', config: { distance: 2.0 } }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('UserSubspace');
  });

  it('wraps head-following objects in UserSubspace composable (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'menu',
          properties: [],
          traits: [{ name: 'follows_head', config: {} }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('UserSubspace {');
    expect(result.activityFile).toContain('head-following');
  });

  // ─── DP3: DRM-protected video via SurfaceEntity ──────────────────

  it('emits DRM video with SurfaceEntity.SurfaceProtection.PROTECTED (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'moviePlayer',
          properties: [],
          traits: [
            {
              name: 'drm_video',
              config: {
                uri: 'https://example.com/movie.mpd',
                license_uri: 'https://license.example.com/widevine',
                stereo_mode: 'SIDE_BY_SIDE',
                shape: 'quad',
              },
            },
          ],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('SurfaceEntity.SurfaceProtection.PROTECTED');
    expect(result.activityFile).toContain('C.WIDEVINE_UUID');
    expect(result.activityFile).toContain('ExoPlayer');
  });

  it('emits sphere shape for 360 DRM video (DP3)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'video360',
          properties: [],
          traits: [
            {
              name: 'drm_video',
              config: { shape: 'sphere', radius: 10 },
            },
          ],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('SurfaceEntity.Shape.Sphere(10f)');
  });

  // ─── DP3: SceneCore import updates ────────────────────────────────

  it('emits GltfModel and GltfModelEntity imports (DP3)', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.activityFile).toContain('import androidx.xr.scenecore.GltfModel');
    expect(result.activityFile).toContain('import androidx.xr.scenecore.GltfModelEntity');
    expect(result.activityFile).toContain('import androidx.xr.scenecore.SurfaceEntity');
    expect(result.activityFile).toContain('import androidx.xr.scenecore.SpatialCapability');
  });

  it('compiles timeline with animation', () => {
    const comp = minimalComposition({
      timelines: [
        {
          name: 'intro',
          loop: true,
          entries: [{ time: 0, action: { kind: 'animate', properties: { opacity: 1 } } }],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('playTimeline_intro');
    expect(result.activityFile).toContain('AnimatorSet');
  });

  it('compiles transition', () => {
    const comp = minimalComposition({
      transitions: [
        {
          name: 'fadeOut',
          properties: [
            { key: 'target', value: 'scene2' },
            { key: 'duration', value: 1.0 },
          ],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.activityFile).toContain('transition_fadeOut');
    expect(result.activityFile).toContain('1000L');
  });

  // ─── State file ───────────────────────────────────────────────────

  it('generates state file with XRSceneState class', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.stateFile).toContain('class XRSceneState : ViewModel()');
    expect(result.stateFile).toContain('package com.test.app');
  });

  it('generates state file with state properties', () => {
    const comp = minimalComposition({
      state: {
        properties: [
          { key: 'score', value: 0 },
          { key: 'active', value: true },
        ],
      },
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.stateFile).toContain('score');
    expect(result.stateFile).toContain('active');
    expect(result.stateFile).toContain('MutableLiveData');
  });

  it('generates state file with XR-specific state', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.stateFile).toContain('spatialCapabilities');
    expect(result.stateFile).toContain('activeEntities');
    expect(result.stateFile).toContain('selectedEntity');
  });

  it('generates state file with reset function', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.stateFile).toContain('fun reset()');
  });

  // ─── Node Factory file ────────────────────────────────────────────

  it('generates node factory file with XRNodeFactory', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.nodeFactoryFile).toContain('object XRNodeFactory');
    expect(result.nodeFactoryFile).toContain('package com.test.app');
  });

  it('generates node factory with GltfModel loading', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.nodeFactoryFile).toContain('loadGltfModel');
    expect(result.nodeFactoryFile).toContain('GltfModel.create');
    expect(result.nodeFactoryFile).toContain('GltfModelEntity');
  });

  it('generates node factory methods for objects', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'Robot',
          properties: [{ key: 'model', value: 'models/robot.glb' }],
          traits: [],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.nodeFactoryFile).toContain('createRobot');
    expect(result.nodeFactoryFile).toContain('models/robot.glb');
  });

  // ─── Manifest file ────────────────────────────────────────────────

  it('generates manifest with XR permissions', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.manifestFile).toContain('uses-permission');
    expect(result.manifestFile).toContain('android.permission.CAMERA');
    expect(result.manifestFile).toContain('android.hardware.xr.headtracking');
  });

  it('generates manifest with XR category', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.manifestFile).toContain('com.google.intent.category.XR');
  });

  it('generates manifest with activity name', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.manifestFile).toContain('.TestActivity');
  });

  it('adds hand tracking permission when hand_tracking trait is used', () => {
    const comp = minimalComposition({
      objects: [
        { name: 'hand', properties: [], traits: [{ name: 'hand_tracking' }], children: [] },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.manifestFile).toContain('android.permission.HAND_TRACKING');
  });

  it('adds face tracking permission when face_tracking trait is used', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'avatar',
          properties: [],
          traits: [{ name: 'face_tracking', config: {} }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.manifestFile).toContain('android.permission.FACE_TRACKING');
  });

  it('adds optional android.hardware.camera.ar when occlusion_mesh trait is used (Film3D ARCore depth)', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'stage',
          properties: [],
          traits: [{ name: 'occlusion_mesh', config: {} }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.manifestFile).toContain('android.hardware.camera.ar');
    expect(result.manifestFile).toMatch(/android\.hardware\.camera\.ar" android:required="false"/);
  });

  it('adds required android.hardware.camera.ar when arCameraHardwareRequired is true', () => {
    const c = new AndroidXRCompiler({
      packageName: 'com.test.app',
      activityName: 'TestActivity',
      arCameraHardwareRequired: true,
    });
    const comp = minimalComposition({
      objects: [
        {
          name: 'stage',
          properties: [],
          traits: [{ name: 'environment_probe', config: {} }],
          children: [],
        },
      ],
    });
    const result = c.compile(comp, 'test-token');
    expect(result.manifestFile).toMatch(/android\.hardware\.camera\.ar" android:required="true"/);
  });

  // ─── Build Gradle file ────────────────────────────────────────────

  it('generates build.gradle.kts with dependencies', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.buildGradle).toContain('dependencies');
    expect(result.buildGradle).toContain('androidx.xr.scenecore');
    expect(result.buildGradle).toContain('androidx.xr.arcore');
  });

  it('generates build.gradle.kts with compose support', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.buildGradle).toContain('compose = true');
    expect(result.buildGradle).toContain('androidx.compose');
  });

  it('generates build.gradle.kts with package name', () => {
    const result = compiler.compile(minimalComposition(), 'test-token');
    expect(result.buildGradle).toContain('com.test.app');
  });

  it('includes Media3 dependencies when DRM video traits are used', () => {
    const comp = minimalComposition({
      objects: [
        {
          name: 'player',
          properties: [],
          traits: [{ name: 'drm_video', config: { uri: 'test.mpd' } }],
          children: [],
        },
      ],
    });
    const result = compiler.compile(comp, 'test-token');
    expect(result.buildGradle).toContain('media3-exoplayer');
  });

  // ─── Convenience export ───────────────────────────────────────────

  it('exports compileToAndroidXR convenience function', async () => {
    const mod = await import('../AndroidXRCompiler');
    expect(mod.compileToAndroidXR).toBeTypeOf('function');
  });
});
