export function compileObjectFactory(compiler: IOSCompiler, obj: HoloObjectDecl): void {
    const varName = compiler.sanitizeName(obj.name);
    const meshType = compiler.findObjProp(obj, 'mesh') || compiler.findObjProp(obj, 'type') || 'cube';

    compiler.emit(`static func make${varName}() -> SCNNode {`);
    compiler.indentLevel++;

    // Geometry
    const geometry = compiler.getSceneKitGeometry(meshType as string);
    compiler.emit(`let geometry = ${geometry}`);
    compiler.emit('');

    // Material
    const color = compiler.findObjProp(obj, 'color');
    const material = compiler.findObjProp(obj, 'material');
    compiler.emit('let material = SCNMaterial()');
    if (color) {
      compiler.emit(`material.diffuse.contents = ${compiler.toUIColor(color)}`);
    } else {
      compiler.emit('material.diffuse.contents = UIColor.systemBlue');
    }
    if (material && typeof material === 'object') {
      const mat = material as Record<string, any>;
      if (mat.metalness !== undefined) {
        compiler.emit(`material.metalness.contents = ${mat.metalness}`);
      }
      if (mat.roughness !== undefined) {
        compiler.emit(`material.roughness.contents = ${mat.roughness}`);
      }
    }
    compiler.emit('geometry.materials = [material]');
    compiler.emit('');

    // Create node
    compiler.emit('let node = SCNNode(geometry: geometry)');
    compiler.emit(`node.name = "${escapeStringValue(obj.name as string, 'Swift')}"`);

    // Transform
    const pos = compiler.findObjProp(obj, 'position');
    if (pos && Array.isArray(pos)) {
      compiler.emit(`node.position = SCNVector3(${pos[0]}, ${pos[1]}, ${pos[2]})`);
    }

    const scale = compiler.findObjProp(obj, 'scale');
    if (scale) {
      if (Array.isArray(scale)) {
        compiler.emit(`node.scale = SCNVector3(${scale[0]}, ${scale[1]}, ${scale[2]})`);
      } else {
        compiler.emit(`node.scale = SCNVector3(${scale}, ${scale}, ${scale})`);
      }
    }

    const rot = compiler.findObjProp(obj, 'rotation');
    if (rot && Array.isArray(rot)) {
      compiler.emit(`node.eulerAngles = SCNVector3(${rot[0]}, ${rot[1]}, ${rot[2]})`);
    }

    // Physics
    if (obj.traits?.some((t) => t.name === 'physics')) {
      compiler.emit('');
      compiler.emit('let physicsBody = SCNPhysicsBody(type: .dynamic, shape: nil)');
      const physicsTrait = obj.traits.find((t) => t.name === 'physics');
      if (physicsTrait?.config?.mass) {
        compiler.emit(`physicsBody.mass = ${physicsTrait.config.mass}`);
      }
      compiler.emit('node.physicsBody = physicsBody');
    } else if (obj.traits?.some((t) => t.name === 'collidable')) {
      compiler.emit('');
      compiler.emit('let physicsBody = SCNPhysicsBody(type: .static, shape: nil)');
      compiler.emit('node.physicsBody = physicsBody');
    }

    compiler.emit('');
    compiler.emit('return node');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');
  }

export function compileLightFactory(compiler: IOSCompiler, light: HoloLight): void {
    const varName = compiler.sanitizeName(light.name);

    compiler.emit(`static func make${varName}() -> SCNNode {`);
    compiler.indentLevel++;

    const lightTypeMap: Record<string, string> = {
      directional: 'SCNLight.LightType.directional',
      point: 'SCNLight.LightType.omni',
      spot: 'SCNLight.LightType.spot',
      ambient: 'SCNLight.LightType.ambient',
    };

    compiler.emit('let light = SCNLight()');
    compiler.emit(`light.type = ${lightTypeMap[light.lightType] || 'SCNLight.LightType.omni'}`);

    for (const prop of light.properties) {
      if (prop.key === 'color') {
        compiler.emit(`light.color = ${compiler.toUIColor(prop.value)}`);
      } else if (prop.key === 'intensity') {
        compiler.emit(`light.intensity = ${(prop.value as number) * 1000}`);
      } else if (prop.key === 'cast_shadow' || prop.key === 'castShadow') {
        compiler.emit(`light.castsShadow = ${prop.value ? 'true' : 'false'}`);
      }
    }

    compiler.emit('');
    compiler.emit('let node = SCNNode()');
    compiler.emit(`node.name = "${escapeStringValue(light.name as string, 'Swift')}"`);
    compiler.emit('node.light = light');

    const posProp = light.properties.find((p) => p.key === 'position');
    if (posProp && Array.isArray(posProp.value)) {
      const pos = posProp.value as number[];
      compiler.emit(`node.position = SCNVector3(${pos[0]}, ${pos[1]}, ${pos[2]})`);
    }

    compiler.emit('');
    compiler.emit('return node');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');
  }

export function compileAudioFactory(compiler: IOSCompiler, audio: HoloAudio): void {
    const varName = compiler.sanitizeName(audio.name);

    compiler.emit(`static func make${varName}() -> SCNAudioSource? {`);
    compiler.indentLevel++;

    const srcProp = audio.properties.find((p) => p.key === 'src' || p.key === 'source');
    if (srcProp) {
      compiler.emit(
        `guard let audioSource = SCNAudioSource(named: "${srcProp.value}") else { return nil }`
      );
    } else {
      compiler.emit('return nil');
      compiler.indentLevel--;
      compiler.emit('}');
      return;
    }

    const loopProp = audio.properties.find((p) => p.key === 'loop');
    if (loopProp) {
      compiler.emit(`audioSource.loops = ${loopProp.value ? 'true' : 'false'}`);
    }

    const volumeProp = audio.properties.find((p) => p.key === 'volume');
    if (volumeProp) {
      compiler.emit(`audioSource.volume = ${volumeProp.value}`);
    }

    compiler.emit('audioSource.isPositional = true');
    compiler.emit('audioSource.load()');
    compiler.emit('return audioSource');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');
  }

export function compileAction(compiler: IOSCompiler, action: { name: string }): void {
    const name = compiler.sanitizeName(action.name);
    compiler.emit(`func ${name}() {`);
    compiler.indentLevel++;
    compiler.emit(`print("[HoloScript] Action: ${escapeStringValue(action.name, 'Swift')}")`);
    compiler.emit('// Action implementation');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');
  }

export function hasGeoTraits(composition: HoloComposition): boolean {
    const geoTraitNames = [
      'geo_anchor',
      'geo_persist',
      'geo_radius',
      'geo_compass_heading',
      'geo_altitude',
      'geo_cloud_anchor',
      'geo_terrain_snap',
      'geo_session_continuity',
      'geo_proximity_trigger',
      'geo_discoverable',
      'geo_arcore_geospatial',
      'geo_arkit_geo_anchor',
    ];
    for (const obj of composition.objects || []) {
      for (const trait of obj.traits || []) {
        const name = typeof trait === 'string' ? trait : trait.name;
        if (geoTraitNames.includes(name)) return true;
      }
    }
    return false;
  }

export function emitGeoAnchorMethods(compiler: IOSCompiler, composition: HoloComposition): void {
    compiler.emit('');
    compiler.emit('// === Geo-Anchor: GPS-pinned persistent holograms ===');
    compiler.emit('private let locationManager = CLLocationManager()');
    compiler.emit('var geoAnchors: [String: ARAnchor] = [:]');
    compiler.emit('');

    // setupGeoAnchors
    compiler.emit('func setupGeoAnchors() {');
    compiler.indentLevel++;
    compiler.emit('locationManager.requestWhenInUseAuthorization()');
    compiler.emit('locationManager.startUpdatingLocation()');
    compiler.emit('');

    const usesARGeoAnchor = compiler.compositionHasTrait(composition, 'geo_arkit_geo_anchor');
    if (usesARGeoAnchor) {
      compiler.emit('// Check ARGeoTrackingConfiguration availability');
      compiler.emit('ARGeoTrackingConfiguration.checkAvailability { available, error in');
      compiler.indentLevel++;
      compiler.emit('if available {');
      compiler.indentLevel++;
      compiler.emit('print("[HoloScript] ARGeoTracking available")');
      compiler.indentLevel--;
      compiler.emit('} else {');
      compiler.indentLevel++;
      compiler.emit(
        'print("[HoloScript] ARGeoTracking not available: \\(error?.localizedDescription ?? \\"unknown\\")")'
      );
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
    }

    // Emit anchor creation for geo_anchor objects
    for (const obj of composition.objects || []) {
      const traits = obj.traits || [];
      const hasGeoAnchor = traits.some((t) => {
        const n = typeof t === 'string' ? t : t.name;
        return n === 'geo_anchor';
      });
      if (!hasGeoAnchor) continue;

      const geoAnchorTrait = traits.find((t) => {
        const n = typeof t === 'string' ? t : t.name;
        return n === 'geo_anchor';
      });
      const config = typeof geoAnchorTrait === 'string' ? {} : geoAnchorTrait?.config || {};
      const lat = (config as Record<string, unknown>).latitude ?? 0.0;
      const lng = (config as Record<string, unknown>).longitude ?? 0.0;

      const altTrait = traits.find((t) => {
        const n = typeof t === 'string' ? t : t.name;
        return n === 'geo_altitude';
      });
      const altConfig = typeof altTrait === 'string' ? {} : altTrait?.config || {};
      const alt = (altConfig as Record<string, unknown>).meters ?? 0.0;

      const headingTrait = traits.find((t) => {
        const n = typeof t === 'string' ? t : t.name;
        return n === 'geo_compass_heading';
      });
      const headingConfig = typeof headingTrait === 'string' ? {} : headingTrait?.config || {};
      const heading = (headingConfig as Record<string, unknown>).degrees ?? 0.0;

      compiler.emit(`// Geo-anchor: ${escapeStringValue(obj.name, 'Swift')}`);
      if (usesARGeoAnchor) {
        compiler.emit(
          `createGeoAnchor(name: "${escapeStringValue(obj.name, 'Swift')}", latitude: ${lat}, longitude: ${lng}, altitude: ${alt}, heading: ${heading})`
        );
      } else {
        compiler.emit(
          `createLocationAnchor(name: "${escapeStringValue(obj.name, 'Swift')}", latitude: ${lat}, longitude: ${lng}, altitude: ${alt})`
        );
      }
    }

    // Restore persisted anchors
    const usesPersist = compiler.compositionHasTrait(composition, 'geo_persist');
    if (usesPersist) {
      compiler.emit('');
      compiler.emit('// Restore persisted ARWorldMap');
      compiler.emit('restoreWorldMap()');
    }

    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // createGeoAnchor (ARGeoAnchor for iOS 14+)
    if (usesARGeoAnchor) {
      compiler.emit(
        'func createGeoAnchor(name: String, latitude: Double, longitude: Double, altitude: Double, heading: Double) {'
      );
      compiler.indentLevel++;
      compiler.emit('guard let arView = arView else { return }');
      compiler.emit(
        'let coordinate = CLLocationCoordinate2D(latitude: latitude, longitude: longitude)'
      );
      compiler.emit('let geoAnchor = ARGeoAnchor(coordinate: coordinate, altitude: altitude)');
      compiler.emit('geoAnchor.name = name');
      compiler.emit('arView.session.add(anchor: geoAnchor)');
      compiler.emit('geoAnchors[name] = geoAnchor');
      compiler.emit(
        'print("[HoloScript] Geo-anchored: \\(name) at (\\(latitude), \\(longitude), \\(altitude))")'
      );
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
    }

    // createLocationAnchor (fallback using CLLocation + local anchor)
    compiler.emit(
      'func createLocationAnchor(name: String, latitude: Double, longitude: Double, altitude: Double) {'
    );
    compiler.indentLevel++;
    compiler.emit('guard let arView = arView else { return }');
    compiler.emit('let _ = CLLocation(latitude: latitude, longitude: longitude)');
    compiler.emit('');
    compiler.emit('// Create anchor at current camera position as placeholder');
    compiler.emit('guard let frame = arView.session.currentFrame else { return }');
    compiler.emit('var translation = matrix_identity_float4x4');
    compiler.emit('translation.columns.3[2] = -1.0 // 1 meter in front');
    compiler.emit('let transform = simd_mul(frame.camera.transform, translation)');
    compiler.emit('let anchor = ARAnchor(name: name, transform: transform)');
    compiler.emit('arView.session.add(anchor: anchor)');
    compiler.emit('geoAnchors[name] = anchor');
    compiler.emit('print("[HoloScript] Location-anchored: \\(name) at (\\(latitude), \\(longitude))")');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // ARWorldMap persistence
    if (usesPersist) {
      compiler.emit('func saveWorldMap() {');
      compiler.indentLevel++;
      compiler.emit('guard let arView = arView else { return }');
      compiler.emit('arView.session.getCurrentWorldMap { worldMap, error in');
      compiler.indentLevel++;
      compiler.emit('guard let map = worldMap else {');
      compiler.indentLevel++;
      compiler.emit(
        'print("[HoloScript] Failed to get world map: \\(error?.localizedDescription ?? \\"unknown\\")")'
      );
      compiler.emit('return');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
      compiler.emit('do {');
      compiler.indentLevel++;
      compiler.emit(
        'let data = try NSKeyedArchiver.archivedData(withRootObject: map, requiringSecureCoding: true)'
      );
      compiler.emit('let url = self.worldMapURL()');
      compiler.emit('try data.write(to: url)');
      compiler.emit('print("[HoloScript] World map saved to \\(url)")');
      compiler.indentLevel--;
      compiler.emit('} catch {');
      compiler.indentLevel++;
      compiler.emit('print("[HoloScript] Failed to save world map: \\(error)")');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');

      compiler.emit('func restoreWorldMap() {');
      compiler.indentLevel++;
      compiler.emit('let url = worldMapURL()');
      compiler.emit('guard let data = try? Data(contentsOf: url),');
      compiler.emit(
        '      let worldMap = try? NSKeyedUnarchiver.unarchivedObject(ofClass: ARWorldMap.self, from: data) else {'
      );
      compiler.indentLevel++;
      compiler.emit('print("[HoloScript] No saved world map found")');
      compiler.emit('return');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
      compiler.emit('guard let arView = arView else { return }');
      compiler.emit('let configuration = ARWorldTrackingConfiguration()');
      compiler.emit('configuration.planeDetection = [.horizontal, .vertical]');
      compiler.emit('configuration.initialWorldMap = worldMap');
      compiler.emit(
        'arView.session.run(configuration, options: [.resetTracking, .removeExistingAnchors])'
      );
      compiler.emit('print("[HoloScript] World map restored with \\(worldMap.anchors.count) anchors")');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');

      compiler.emit('private func worldMapURL() -> URL {');
      compiler.indentLevel++;
      compiler.emit(
        'let documentsPath = FileManager.default.urls(for: .documentDirectory, in: .userDomainMask).first!'
      );
      compiler.emit('return documentsPath.appendingPathComponent("holoscript_worldmap.arworldmap")');
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
    }
  }

