export function generateSceneFile(compiler: IOSCompiler, composition: HoloComposition): string {
    compiler.lines = [];
    compiler.indentLevel = 0;

    compiler.emit('// Auto-generated by HoloScript IOSCompiler');
    compiler.emit(`// Scene: ${escapeStringValue(composition.name as string, 'Swift')}`);
    compiler.emit('');
    compiler.emit('import Foundation');
    compiler.emit('import SceneKit');
    compiler.emit('import ARKit');
    compiler.emit('');

    compiler.emit(`enum ${compiler.options.className}Objects {`);
    compiler.indentLevel++;

    // Generate node creation methods for each object
    for (const obj of composition.objects || []) {
      compileObjectFactory(compiler, obj);
    }

    compiler.emit('');
    compiler.emit('// Factory method to create all scene objects');
    compiler.emit('static func createSceneNodes() -> [String: SCNNode] {');
    compiler.indentLevel++;
    compiler.emit('var nodes: [String: SCNNode] = [:]');
    for (const obj of composition.objects || []) {
      const varName = compiler.sanitizeName(obj.name);
      compiler.emit(
        `nodes["${escapeStringValue(obj.name as string, 'Swift')}"] = make${varName}()`
      );
    }
    compiler.emit('return nodes');
    compiler.indentLevel--;
    compiler.emit('}');

    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // Light factories
    if (composition.lights?.length) {
      compiler.emit('enum SceneLights {');
      compiler.indentLevel++;
      for (const light of composition.lights) {
        compileLightFactory(compiler, light);
      }
      compiler.indentLevel--;
      compiler.emit('}');
      compiler.emit('');
    }

    // Audio factories
    if (composition.audio?.length) {
      compiler.emit('enum SceneAudio {');
      compiler.indentLevel++;
      for (const audio of composition.audio) {
        compileAudioFactory(compiler, audio);
      }
      compiler.indentLevel--;
      compiler.emit('}');
    }

    return compiler.lines.join('\n');
  }

export function generateStateFile(compiler: IOSCompiler, composition: HoloComposition): string {
    compiler.lines = [];
    compiler.indentLevel = 0;

    compiler.emit('// Auto-generated by HoloScript IOSCompiler');
    compiler.emit(`// State: ${escapeStringValue(composition.name as string, 'Swift')}`);
    compiler.emit('');
    compiler.emit('import Foundation');
    compiler.emit('import SceneKit');
    compiler.emit('import ARKit');
    if (compiler.options.useCombine) {
      compiler.emit('import Combine');
    }
    compiler.emit('');

    compiler.emit('class SceneState: ObservableObject {');
    compiler.indentLevel++;

    // Published state properties
    if (composition.state) {
      compiler.emit('// === Published State ===');
      for (const prop of composition.state.properties) {
        const swiftType = compiler.toSwiftType(prop.value);
        const swiftValue = compiler.toSwiftValue(prop.value);
        compiler.emit(
          `@Published var ${escapeStringValue(prop.key as string, 'Swift')}: ${swiftType} = ${swiftValue}`
        );
      }
      compiler.emit('');
    }

    // AR references
    compiler.emit('// === AR References ===');
    compiler.emit('weak var arView: ARSCNView?');
    compiler.emit('var placedNodes: [String: SCNNode] = [:]');
    compiler.emit('var detectedPlanes: [UUID: SCNNode] = [:]');
    compiler.emit('');

    // Init
    compiler.emit('init() {');
    compiler.indentLevel++;
    compiler.emit('print("[HoloScript] Scene state initialized")');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // Place object method
    compiler.emit('func placeObject(at transform: simd_float4x4) {');
    compiler.indentLevel++;
    compiler.emit('guard let arView = arView else { return }');
    compiler.emit('');
    compiler.emit(`let nodes = ${compiler.options.className}Objects.createSceneNodes()`);
    compiler.emit('guard let node = nodes.values.first else { return }');
    compiler.emit('');
    compiler.emit('node.simdWorldTransform = transform');
    compiler.emit('arView.scene.rootNode.addChildNode(node)');
    compiler.emit('');
    compiler.emit('let id = UUID().uuidString');
    compiler.emit('placedNodes[id] = node');
    compiler.emit('print("[HoloScript] Placed object: \\(id)")');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // Handle node tap
    compiler.emit('func handleNodeTap(_ node: SCNNode) {');
    compiler.indentLevel++;
    compiler.emit('print("[HoloScript] Tapped node: \\(node.name ?? "unnamed")")');
    compiler.emit('');
    compiler.emit('// Visual feedback');
    compiler.emit('let originalScale = node.scale');
    compiler.emit('SCNTransaction.begin()');
    compiler.emit('SCNTransaction.animationDuration = 0.1');
    compiler.emit(
      'node.scale = SCNVector3(originalScale[0] * 1.2, originalScale[1] * 1.2, originalScale[2] * 1.2)'
    );
    compiler.emit('SCNTransaction.commit()');
    compiler.emit('');
    compiler.emit('DispatchQueue.main.asyncAfter(deadline: .now() + 0.1) {');
    compiler.indentLevel++;
    compiler.emit('SCNTransaction.begin()');
    compiler.emit('SCNTransaction.animationDuration = 0.1');
    compiler.emit('node.scale = originalScale');
    compiler.emit('SCNTransaction.commit()');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // Plane detection
    compiler.emit('func didDetectPlane(_ anchor: ARPlaneAnchor, node: SCNNode) {');
    compiler.indentLevel++;
    compiler.emit(
      'let plane = SCNPlane(width: CGFloat(anchor.planeExtent.width), height: CGFloat(anchor.planeExtent.height))'
    );
    compiler.emit('plane.firstMaterial?.diffuse.contents = UIColor.white.withAlphaComponent(0.3)');
    compiler.emit('');
    compiler.emit('let planeNode = SCNNode(geometry: plane)');
    compiler.emit('planeNode.eulerAngles[0] = -.pi / 2');
    compiler.emit('planeNode.position = SCNVector3(anchor.center[0], 0, anchor.center[2])');
    compiler.emit('');
    compiler.emit('node.addChildNode(planeNode)');
    compiler.emit('detectedPlanes[anchor.identifier] = planeNode');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    compiler.emit('func didUpdatePlane(_ anchor: ARPlaneAnchor, node: SCNNode) {');
    compiler.indentLevel++;
    compiler.emit('guard let planeNode = detectedPlanes[anchor.identifier],');
    compiler.emit('      let plane = planeNode.geometry as? SCNPlane else { return }');
    compiler.emit('');
    compiler.emit('plane.width = CGFloat(anchor.planeExtent.width)');
    compiler.emit('plane.height = CGFloat(anchor.planeExtent.height)');
    compiler.emit('planeNode.position = SCNVector3(anchor.center[0], 0, anchor.center[2])');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('');

    // Reset scene
    compiler.emit('func resetScene() {');
    compiler.indentLevel++;
    compiler.emit('guard let arView = arView else { return }');
    compiler.emit('');
    compiler.emit('// Remove placed nodes');
    compiler.emit('for node in placedNodes.values {');
    compiler.indentLevel++;
    compiler.emit('node.removeFromParentNode()');
    compiler.indentLevel--;
    compiler.emit('}');
    compiler.emit('placedNodes.removeAll()');
    compiler.emit('');
    compiler.emit('// Reset AR session');
    compiler.emit('let configuration = ARWorldTrackingConfiguration()');
    compiler.emit('configuration.planeDetection = [.horizontal, .vertical]');
    compiler.emit(
      'arView.session.run(configuration, options: [.resetTracking, .removeExistingAnchors])'
    );
    compiler.emit('');
    compiler.emit('print("[HoloScript] Scene reset")');
    compiler.indentLevel--;
    compiler.emit('}');

    // Geo-anchor methods
    if (hasGeoTraits(composition)) {
      emitGeoAnchorMethods(compiler, composition);
    }

    // Actions from composition state
    if (composition.logic?.actions) {
      compiler.emit('');
      compiler.emit('// === Actions ===');
      for (const action of composition.logic.actions) {
        compileAction(compiler, action);
      }
    }

    compiler.indentLevel--;
    compiler.emit('}');

    return compiler.lines.join('\n');
  }

