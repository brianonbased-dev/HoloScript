export function hasFaceTrackingTraits(composition: HoloComposition): boolean {
    const faceTraitNames: ReadonlyArray<string> = FACE_TRACKING_TRAITS;
    for (const obj of composition.objects || []) {
      for (const trait of obj.traits || []) {
        const name = typeof trait === 'string' ? trait : trait.name;
        if (faceTraitNames.includes(name)) return true;
      }
    }
    return false;
  }

