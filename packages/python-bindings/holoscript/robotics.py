"""
HoloScript Robotics Module

Provides utilities for the robotics community to work with HoloScript
in ROS 2, Gazebo, and robotic simulation contexts.

Supports:
- URDF/SDF export from HoloScript scenes
- ROS 2 launch file generation
- Gazebo world file creation
- Robot trait templates
"""

from typing import Dict, Any, List, Optional
from dataclasses import dataclass, field


@dataclass
class URDFExportResult:
    """Result of exporting a HoloScript scene to URDF format."""
    success: bool
    urdf_content: str = ""
    robot_name: str = "holoscript_robot"
    link_count: int = 0
    joint_count: int = 0
    errors: List[str] = field(default_factory=list)
    warnings: List[str] = field(default_factory=list)


@dataclass
class SDFExportResult:
    """Result of exporting a HoloScript scene to SDF format."""
    success: bool
    sdf_content: str = ""
    world_name: str = "holoscript_world"
    model_count: int = 0
    errors: List[str] = field(default_factory=list)
    warnings: List[str] = field(default_factory=list)


@dataclass
class ROSLaunchResult:
    """Result of generating a ROS 2 launch file."""
    success: bool
    launch_content: str = ""
    package_name: str = "holoscript_scene"
    nodes: List[str] = field(default_factory=list)
    errors: List[str] = field(default_factory=list)


