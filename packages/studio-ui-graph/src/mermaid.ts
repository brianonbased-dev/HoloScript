import type { PageGraph } from './emit.js';
import type { ComponentNode } from './tree.js';

/**
 * v0.6: Mermaid flowchart serializer. Renders the page → component graph as
 * a top-down `flowchart TD` with one subgraph per page. Mermaid `LR` would be
 * wider; `TD` keeps long component chains readable. Identifiers are sanitized
 * to mermaid-safe form (alphanumeric + underscore).
 *
 * Output is markdown-fenced so it pastes directly into GitHub PRs, the agent
 * dashboard, or a Studio dev page that renders Mermaid.
 */
export function emitMermaid(graphs: PageGraph[]): string {
  const lines: string[] = [];
  lines.push('```mermaid');
  lines.push('flowchart TD');
  lines.push('  %% AUTO-GENERATED by @holoscript/studio-ui-graph — do not edit');
  for (const g of graphs) {
    const pageNodeId = `page_${safe(g.page.id)}`;
    lines.push(`  subgraph ${safe(g.page.id)}["${g.page.id}<br/>${escapeLabel(g.page.route)}"]`);
    lines.push(`    direction LR`);
    const seen = new Set<string>();
    emitNode(g.tree, pageNodeId, seen, lines, 4);
    lines.push('  end');
  }
  lines.push('```');
  return lines.join('\n') + '\n';
}

function emitNode(node: ComponentNode, parentId: string, seen: Set<string>, out: string[], indent: number): void {
  const pad = ' '.repeat(indent);
  const id = `${parentId}_${safe(node.name)}`;
  const isRoot = node.name === 'Page';
  if (!isRoot && !seen.has(id)) {
    out.push(`${pad}${id}["${escapeLabel(node.name)}"]`);
    seen.add(id);
    out.push(`${pad}${parentId} --> ${id}`);
  }
  for (const child of node.children) {
    emitNode(child, isRoot ? parentId : id, seen, out, indent);
  }
}

function safe(s: string): string {
  return s.replace(/[^A-Za-z0-9_]/g, '_').replace(/^(\d)/, '_$1');
}

function escapeLabel(s: string): string {
  return s.replace(/"/g, "'");
}
