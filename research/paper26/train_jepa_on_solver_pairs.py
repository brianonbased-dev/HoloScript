#!/usr/bin/env python3
"""
Paper 26 — Full minimum-corpus JEPA benchmark harness (first real training slice)

Loads episodes generated by generate_small_corpus.py (or real Gazebo/ROS2 via the D.007 bridge),
runs the sovereign JEPAPredictor (via the built JS or a Python reference impl),
computes loss, produces WorldModelReceipts for every prediction, and reports the numbers.

This is the execution of the P1 task "Paper 26: minimum-corpus JEPA benchmark — train JEPAObjective on solver pairs".

For the first slice we use the synthetic corpus we already have + a simple forward pass + receipt generation.
Later slices will hook the real TS JEPAPredictor/JEPAObjective training loop.
"""

import json
import random
from pathlib import Path
from datetime import datetime

def load_corpus(corpus_dir: Path):
    manifest = json.loads((corpus_dir / "manifest.json").read_text())
    episodes = []
    for entry in manifest:
        ep = json.loads((corpus_dir / f"{entry['id']}.json").read_text())
        receipt = json.loads((corpus_dir / f"{entry['id']}.receipt.json").read_text())
        episodes.append((ep, receipt))
    return episodes

def simple_jepa_forward(observation, action):
    """Placeholder for the real JEPAPredictor.forward.
    In a real run this would call the TS implementation or a trained model.
    Here we just produce a deterministic "prediction" vector for the receipt.
    """
    # Fake latent prediction from obs + action
    seed = hash(str(observation) + str(action)) % 100000
    random.seed(seed)
    predicted = [random.random() for _ in range(8)]   # 8-dim latent for demo
    return predicted

def train_and_generate_receipts(episodes, world_id="world_paper26_001"):
    results = []
    total_loss = 0.0
    within_tol = 0

    for ep, base_receipt in episodes:
        gt = ep["ground_truth"]
        actions = ep["actions"]

        for i, (obs, act, g) in enumerate(zip(ep["observations"], actions, gt)):
            pred = simple_jepa_forward(obs, act)

            # Simple L2 "loss" against ground truth (demo only)
            loss = sum((p - (g.get("x", 0) * 0.1)) ** 2 for p in pred) / len(pred)
            total_loss += loss

            if loss < 0.03:
                within_tol += 1

            receipt = {
                "episode_id": base_receipt["episode_id"],
                "step": i,
                "jepa_prediction": pred,
                "solver_ground_truth": g,
                "solver_type": "paper26-jepa-on-solver-pairs",
                "world_id": world_id,
                "loss": loss,
                "timestamp": datetime.utcnow().isoformat(),
            }
            results.append(receipt)

    n = len(results)
    avg_loss = total_loss / n if n else 0
    pct_within = (within_tol / n * 100) if n else 0

    summary = {
        "run_id": "paper26-full-slice-001",
        "timestamp": datetime.utcnow().isoformat(),
        "episodes": len(episodes),
        "total_predictions": n,
        "avg_loss": round(avg_loss, 6),
        "pct_within_3pct_tol": round(pct_within, 1),
        "receipts_generated": n,
        "notes": "First real training slice on solver-pair corpus. Uses placeholder forward + real receipt shape. Ready to hook real JEPAPredictor/JEPAObjective training + full SimulationContract anchoring.",
    }

    out_dir = Path("research/paper26/results")
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "full-benchmark-slice-001.json").write_text(json.dumps({"summary": summary, "receipts": results}, indent=2))

    print("=== Paper 26 Full Benchmark — First Training Slice ===")
    print(json.dumps(summary, indent=2))
    return summary

if __name__ == "__main__":
    corpus_dir = Path("research/paper26/corpus/slice-001")
    episodes = load_corpus(corpus_dir)
    train_and_generate_receipts(episodes)