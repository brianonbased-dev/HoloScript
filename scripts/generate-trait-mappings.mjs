#!/usr/bin/env node
/**
 * generate-trait-mappings.mjs
 *
 * Reads trait constants + compiler trait maps from source and generates
 * docs/trait-mappings.md with current data. No hardcoded counts.
 *
 * Usage: node scripts/generate-trait-mappings.mjs
 */

import { readFileSync, readdirSync, writeFileSync } from 'fs';
import { join, basename } from 'path';

const ROOT = new URL('..', import.meta.url).pathname.replace(/^\/([A-Z]:)/, '$1');
const CONSTANTS_DIR = join(ROOT, 'packages/core/src/traits/constants');
const COMPILER_DIR = join(ROOT, 'packages/core/src/compiler');

// ─── 1. Read all trait categories ───────────────────────────────────────────

function readTraitCategories() {
  const files = readdirSync(CONSTANTS_DIR)
    .filter(f => f.endsWith('.ts') && f !== 'index.ts' && !f.includes('.test.'));

  const categories = [];

  for (const file of files) {
    const content = readFileSync(join(CONSTANTS_DIR, file), 'utf8');
    const name = basename(file, '.ts');

    // Extract trait names from the array
    const traits = [];
    const matches = content.matchAll(/'([a-z][a-z0-9_]*)'/g);
    for (const m of matches) {
      traits.push(m[1]);
    }

    // Extract the export name
    const exportMatch = content.match(/export const (\w+)/);
    const exportName = exportMatch ? exportMatch[1] : name.toUpperCase();

    categories.push({ name, exportName, traits, file });
  }

  return categories.sort((a, b) => a.name.localeCompare(b.name));
}

// ─── 2. Read compiler trait maps ────────────────────────────────────────────

function readCompilerTraitSupport() {
  const compilers = {};

  // Dedicated trait map files
  const traitMapFiles = readdirSync(COMPILER_DIR)
    .filter(f => f.includes('TraitMap') && f.endsWith('.ts') && !f.includes('.test.'));

  for (const file of traitMapFiles) {
    const content = readFileSync(join(COMPILER_DIR, file), 'utf8');
    const compilerName = basename(file, '.ts').replace('TraitMap', '');

    const traits = new Set();
    const matches = content.matchAll(/'([a-z][a-z0-9_]*)'/g);
    for (const m of matches) {
      traits.add(m[1]);
    }
    compilers[compilerName] = { type: 'dedicated-map', traits, file };
  }

  // Inline trait handling in compilers
  const compilerFiles = readdirSync(COMPILER_DIR)
    .filter(f => f.endsWith('Compiler.ts') && !f.includes('Base') && !f.includes('.test.'));

  for (const file of compilerFiles) {
    const content = readFileSync(join(COMPILER_DIR, file), 'utf8');
    const compilerName = basename(file, '.ts');

    // Skip if already handled by dedicated map
    const mapName = compilerName.replace('Compiler', '');
    if (compilers[mapName]) continue;

    const traits = new Set();
    // Match trait.name === 'xxx' or trait.name === "xxx"
    const matches = content.matchAll(/trait\.name\s*===?\s*['"]([a-z][a-z0-9_]*)['"]/g);
    for (const m of matches) {
      traits.add(m[1]);
    }
    // Match case 'xxx'
    const caseMatches = content.matchAll(/case\s+['"]([a-z][a-z0-9_]*)['"]/g);
    for (const m of caseMatches) {
      traits.add(m[1]);
    }

    if (traits.size > 0) {
      compilers[compilerName] = { type: 'inline', traits, file };
    }
  }

  return compilers;
}

// ─── 3. Generate markdown ───────────────────────────────────────────────────

function generateMarkdown(categories, compilers) {
  const totalTraits = categories.reduce((sum, c) => sum + c.traits.length, 0);
  const compilerNames = Object.keys(compilers).sort();
  const now = new Date().toISOString().split('T')[0];

  let md = `# HoloScript Trait Mappings — Auto-Generated

> **Generated**: ${now} by \`node scripts/generate-trait-mappings.mjs\`
> **Categories**: ${categories.length} | **Traits**: ${totalTraits} | **Compilers with trait support**: ${compilerNames.length}
>
> Re-run the generator to update. Do not edit this file manually.

## Trait Resolution Pipeline

\`\`\`
.holo source → Parser → Compiler → Runtime
  @trait_name    tokenize    compile to target-native    register handler
\`\`\`

## Compiler Trait Coverage

| Compiler | Type | Traits Mapped | Source |
|----------|------|--------------|--------|
`;

  for (const name of compilerNames) {
    const c = compilers[name];
    md += `| ${name} | ${c.type} | ${c.traits.size} | \`${c.file}\` |\n`;
  }

  md += `\n## Categories (${categories.length})\n\n`;

  // Build a compact table: category, count, sample traits, which compilers support them
  md += `| Category | Traits | Sample | Compiler Coverage |\n`;
  md += `|----------|--------|--------|-------------------|\n`;

  for (const cat of categories) {
    const sample = cat.traits.slice(0, 3).map(t => `\`${t}\``).join(', ');
    const more = cat.traits.length > 3 ? ` +${cat.traits.length - 3}` : '';

    // Check which compilers handle any trait from this category
    const coverage = [];
    for (const [compName, comp] of Object.entries(compilers)) {
      const overlap = cat.traits.filter(t => comp.traits.has(t));
      if (overlap.length > 0) {
        const pct = Math.round((overlap.length / cat.traits.length) * 100);
        coverage.push(`${compName.replace('Compiler', '')}(${pct}%)`);
      }
    }

    md += `| ${cat.name} | ${cat.traits.length} | ${sample}${more} | ${coverage.join(', ') || '—'} |\n`;
  }

  // Full trait list per category
  md += `\n---\n\n## Full Trait Lists\n\n`;

  for (const cat of categories) {
    md += `### ${cat.name} (${cat.traits.length})\n\n`;
    md += `Source: \`traits/constants/${cat.file}\` → \`${cat.exportName}\`\n\n`;
    md += `\`\`\`\n${cat.traits.join(', ')}\n\`\`\`\n\n`;
  }

  // Verification
  md += `## Verification\n\n`;
  md += `\`\`\`bash\n`;
  md += `# Regenerate this file\nnode scripts/generate-trait-mappings.mjs\n\n`;
  md += `# Count traits from source\ngrep -roh "'[a-z_]*'" packages/core/src/traits/constants/*.ts | sort -u | wc -l\n\n`;
  md += `# Count categories\nls packages/core/src/traits/constants/*.ts | grep -v index | wc -l\n`;
  md += `\`\`\`\n`;

  return md;
}

// ─── Main ───────────────────────────────────────────────────────────────────

const categories = readTraitCategories();
const compilers = readCompilerTraitSupport();
const md = generateMarkdown(categories, compilers);

const outPath = join(ROOT, 'docs/trait-mappings.md');
writeFileSync(outPath, md);

console.log(`Generated docs/trait-mappings.md`);
console.log(`  Categories: ${categories.length}`);
console.log(`  Traits: ${categories.reduce((s, c) => s + c.traits.length, 0)}`);
console.log(`  Compilers: ${Object.keys(compilers).length}`);
